//! C code generation backend for Kira.
//!
//! Translates Kira IR into C source code that can be compiled with any
//! standard C compiler (gcc, clang).
//!
//! Each IR function becomes a C function. SSA values become local variables.
//! Basic blocks become labeled sections with goto-based control flow.
//!
//! Type-specific code generation:
//! - Function signatures use proper C types (int32_t, double, bool, etc.)
//!   derived from IR param type_name and return_type_name
//! - Record struct fields use proper C types from FieldDecl.type_name
//! - Constants use type-appropriate C declarations
//! - SSA variables use uniform kira_int internally for compound type compat
//!
//! Compound types are heap-allocated and referenced via intptr_t casts:
//! - Tuples: kira_int* with elements at [0..N)
//! - Arrays: kira_int* with length at [0], elements at [1..N]
//! - Records: kira_int* with fields at [0..N) indexed by declaration order
//! - Variants: kira_int* with tag at [0], payload fields at [1..N]
//! - Closures: kira_int* with function pointer at [0], captures at [1..N]
//! - Strings: char* cast through intptr_t; concat via malloc+strcat

const std = @import("std");
const Allocator = std.mem.Allocator;
const ir = @import("ir/ir.zig");
const Module = ir.Module;
const Function = ir.Function;
const Instruction = ir.Instruction;
const Terminator = ir.Terminator;
const ValueRef = ir.ValueRef;
const BlockId = ir.BlockId;

pub const CodeGenError = error{
    OutOfMemory,
    UnsupportedInstruction,
    InvalidIR,
};

/// C code generator that emits C source from Kira IR.
pub const CCodeGen = struct {
    allocator: Allocator,
    /// Output buffer for generated C code.
    output: std.ArrayListUnmanaged(u8),
    /// Current indentation level.
    indent_level: u32,
    /// Reference to the module being generated (set during generateModule).
    current_module: ?*const Module,
    /// SSA values known to hold string values (for string comparison via strcmp).
    string_values: std.AutoArrayHashMapUnmanaged(ValueRef, void),
    /// SSA values known to hold boolean values (for bool-to-string in interpolation).
    bool_values: std.AutoArrayHashMapUnmanaged(ValueRef, void),

    pub fn init(allocator: Allocator) CCodeGen {
        return .{
            .allocator = allocator,
            .output = .{},
            .indent_level = 0,
            .current_module = null,
            .string_values = .{},
            .bool_values = .{},
        };
    }

    pub fn deinit(self: *CCodeGen) void {
        self.output.deinit(self.allocator);
        self.string_values.deinit(self.allocator);
        self.bool_values.deinit(self.allocator);
    }

    /// Get the generated C source code.
    pub fn getOutput(self: *const CCodeGen) []const u8 {
        return self.output.items;
    }

    /// Generate C code for an entire IR module.
    pub fn generateModule(self: *CCodeGen, module: *const Module) CodeGenError!void {
        self.current_module = module;
        try self.emitPreamble();
        try self.emitTypeDecls(module);
        try self.emitFunctionDeclarations(module);
        try self.emitConstants(module);
        for (module.functions.items) |*func| {
            try self.emitFunction(func);
        }
        try self.emitEntryPoint(module);
    }

    /// Emit a C main() entry point that calls kira_main().
    fn emitEntryPoint(self: *CCodeGen, module: *const Module) CodeGenError!void {
        // Check if there's a kira main function
        if (module.function_map.get("main")) |idx| {
            const func = &module.functions.items[idx];
            const has_return = !std.mem.eql(u8, func.return_type_name, "void");

            try self.write("int main(int argc, char** argv) {\n");
            self.indent_level += 1;
            try self.writeIndent();
            try self.write("KIRA_GC_INIT();\n");
            try self.writeIndent();
            try self.write("_kira_argc = argc;\n");
            try self.writeIndent();
            try self.write("_kira_argv = argv;\n");
            try self.writeIndent();
            if (has_return) {
                try self.writeFmt("kira_int result = kira_main(0);\n", .{});
                try self.writeIndent();
                try self.write("printf(\"%lld\\n\", (long long)result);\n");
                try self.writeIndent();
                try self.write("return 0;\n");
            } else {
                try self.write("kira_main(0);\n");
                try self.writeIndent();
                try self.write("return 0;\n");
            }
            self.indent_level -= 1;
            try self.write("}\n");
        }
    }

    // ----------------------------------------------------------------
    // Preamble
    // ----------------------------------------------------------------

    fn emitPreamble(self: *CCodeGen) CodeGenError!void {
        try self.write("/* Generated by Kira compiler */\n");
        try self.write("#include <stdint.h>\n");
        try self.write("#include <stdbool.h>\n");
        try self.write("#include <stdio.h>\n");
        try self.write("#include <stdlib.h>\n");
        try self.write("#include <string.h>\n");
        try self.write("#include <math.h>\n\n");
        // GC abstraction: compile with -DKIRA_NO_GC to use plain malloc
        try self.write("/* GC configuration */\n");
        try self.write("#ifdef KIRA_NO_GC\n");
        try self.write("#define KIRA_ALLOC(sz) malloc(sz)\n");
        try self.write("#define KIRA_GC_INIT() ((void)0)\n");
        try self.write("#else\n");
        try self.write("#include <gc.h>\n");
        try self.write("#define KIRA_ALLOC(sz) GC_MALLOC(sz)\n");
        try self.write("#define KIRA_GC_INIT() GC_INIT()\n");
        try self.write("#endif\n\n");
        try self.write("/* Kira runtime types */\n");
        try self.write("typedef int64_t kira_int;\n");
        try self.write("typedef double kira_float;\n");
        try self.write("typedef bool kira_bool;\n");
        try self.write("typedef uint32_t kira_char;\n");
        try self.write("typedef const char* kira_string;\n");
        try self.write("typedef void kira_void;\n\n");

        // Effect tracking system
        try self.write("/* Kira effect tracking */\n");
        try self.write("#ifdef KIRA_DEBUG_EFFECTS\n");
        try self.write("static int _kira_pure_depth = 0;\n");
        try self.write("#define KIRA_ENTER_PURE() (_kira_pure_depth++)\n");
        try self.write("#define KIRA_LEAVE_PURE() (_kira_pure_depth--)\n");
        try self.write("#define KIRA_ASSERT_EFFECT(fn_name) do { \\\n");
        try self.write("    if (_kira_pure_depth > 0) { \\\n");
        try self.write("        fprintf(stderr, \"Kira effect violation: %s called from pure context\\n\", fn_name); \\\n");
        try self.write("        abort(); \\\n");
        try self.write("    } \\\n");
        try self.write("} while(0)\n");
        try self.write("#else\n");
        try self.write("#define KIRA_ENTER_PURE() ((void)0)\n");
        try self.write("#define KIRA_LEAVE_PURE() ((void)0)\n");
        try self.write("#define KIRA_ASSERT_EFFECT(fn_name) ((void)0)\n");
        try self.write("#endif\n\n");

        try self.write("/* Kira runtime IO functions (effect-only) */\n");
        try self.write("static void kira_println(kira_string s) { KIRA_ASSERT_EFFECT(\"println\"); printf(\"%s\\n\", s); }\n");
        try self.write("static void kira_print(kira_string s) { KIRA_ASSERT_EFFECT(\"print\"); printf(\"%s\", s); }\n");
        try self.write("static void kira_println_int(kira_int n) { KIRA_ASSERT_EFFECT(\"println_int\"); printf(\"%lld\\n\", (long long)n); }\n");
        try self.write("static void kira_print_int(kira_int n) { KIRA_ASSERT_EFFECT(\"print_int\"); printf(\"%lld\", (long long)n); }\n");
        try self.write("static kira_string kira_int_to_string(kira_int n) {\n");
        try self.write("    char* s = (char*)KIRA_ALLOC(32);\n");
        try self.write("    snprintf(s, 32, \"%lld\", (long long)n);\n");
        try self.write("    return s;\n");
        try self.write("}\n");
        try self.write("static kira_string kira_char_to_string(kira_int c) {\n");
        try self.write("    char* s = (char*)KIRA_ALLOC(5);\n");
        try self.write("    uint32_t cp = (uint32_t)c;\n");
        try self.write("    if (cp < 0x80) { s[0] = (char)cp; s[1] = '\\0'; }\n");
        try self.write("    else if (cp < 0x800) { s[0] = (char)(0xC0|(cp>>6)); s[1] = (char)(0x80|(cp&0x3F)); s[2] = '\\0'; }\n");
        try self.write("    else if (cp < 0x10000) { s[0] = (char)(0xE0|(cp>>12)); s[1] = (char)(0x80|((cp>>6)&0x3F)); s[2] = (char)(0x80|(cp&0x3F)); s[3] = '\\0'; }\n");
        try self.write("    else { s[0] = (char)(0xF0|(cp>>18)); s[1] = (char)(0x80|((cp>>12)&0x3F)); s[2] = (char)(0x80|((cp>>6)&0x3F)); s[3] = (char)(0x80|(cp&0x3F)); s[4] = '\\0'; }\n");
        try self.write("    return s;\n");
        try self.write("}\n");
        try self.write("static void kira_assert(kira_int cond, kira_string msg) {\n");
        try self.write("    if (!cond) { fprintf(stderr, \"Assertion failed: %s\\n\", msg); abort(); }\n");
        try self.write("}\n\n");

        // String helper functions
        try self.write("#include <ctype.h>\n");
        try self.write("static kira_string kira_string_trim(kira_string s) {\n");
        try self.write("    while (*s && isspace((unsigned char)*s)) s++;\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    while (len > 0 && isspace((unsigned char)s[len-1])) len--;\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    memcpy(r, s, len); r[len] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_int kira_string_split(kira_string str, kira_string delim) {\n");
        try self.write("    size_t dlen = strlen(delim);\n");
        try self.write("    size_t count = 1;\n");
        try self.write("    const char* p = str;\n");
        try self.write("    if (dlen > 0) { while ((p = strstr(p, delim)) != NULL) { count++; p += dlen; } }\n");
        try self.write("    kira_int* arr = (kira_int*)KIRA_ALLOC((count + 1) * sizeof(kira_int));\n");
        try self.write("    arr[0] = (kira_int)count;\n");
        try self.write("    p = str;\n");
        try self.write("    for (size_t i = 0; i < count; i++) {\n");
        try self.write("        const char* next = (dlen > 0) ? strstr(p, delim) : NULL;\n");
        try self.write("        size_t slen = next ? (size_t)(next - p) : strlen(p);\n");
        try self.write("        char* seg = (char*)KIRA_ALLOC(slen + 1);\n");
        try self.write("        memcpy(seg, p, slen); seg[slen] = '\\0';\n");
        try self.write("        arr[i + 1] = (kira_int)(intptr_t)seg;\n");
        try self.write("        p = next ? next + dlen : p + slen;\n");
        try self.write("    }\n");
        try self.write("    return (kira_int)(intptr_t)arr;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_substring(kira_string s, kira_int start, kira_int end) {\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    if (start < 0) start = 0;\n");
        try self.write("    if ((size_t)end > len) end = (kira_int)len;\n");
        try self.write("    if (start >= end) { char* r = (char*)KIRA_ALLOC(1); r[0] = '\\0'; return r; }\n");
        try self.write("    size_t slen = (size_t)(end - start);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(slen + 1);\n");
        try self.write("    memcpy(r, s + start, slen); r[slen] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_to_upper(kira_string s) {\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    for (size_t i = 0; i < len; i++) r[i] = (char)toupper((unsigned char)s[i]);\n");
        try self.write("    r[len] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_to_lower(kira_string s) {\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    for (size_t i = 0; i < len; i++) r[i] = (char)tolower((unsigned char)s[i]);\n");
        try self.write("    r[len] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_replace(kira_string s, kira_string old, kira_string new_s) {\n");
        try self.write("    size_t olen = strlen(old), nlen = strlen(new_s), slen = strlen(s);\n");
        try self.write("    if (olen == 0) { char* r = (char*)KIRA_ALLOC(slen+1); memcpy(r,s,slen+1); return r; }\n");
        try self.write("    size_t count = 0;\n");
        try self.write("    const char* p = s;\n");
        try self.write("    while ((p = strstr(p, old)) != NULL) { count++; p += olen; }\n");
        try self.write("    size_t rlen = slen + count * (nlen - olen);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(rlen + 1);\n");
        try self.write("    char* w = r; p = s;\n");
        try self.write("    while (*p) {\n");
        try self.write("        if (strncmp(p, old, olen) == 0) { memcpy(w, new_s, nlen); w += nlen; p += olen; }\n");
        try self.write("        else { *w++ = *p++; }\n");
        try self.write("    }\n");
        try self.write("    *w = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n\n");

        // --- Tier 5: Filesystem helpers ---
        try self.write("#include <unistd.h>\n");
        try self.write("#include <sys/time.h>\n\n");

        // Result variant encoding: Ok=tag 0, Err=tag 1
        // Layout: kira_int* [tag][payload]
        try self.write("/* Make Ok(value) variant: tag=0, payload at [1] */\n");
        try self.write("static kira_int kira_make_ok(kira_int value) {\n");
        try self.write("    kira_int* v = (kira_int*)KIRA_ALLOC(2 * sizeof(kira_int));\n");
        try self.write("    v[0] = 0; v[1] = value;\n");
        try self.write("    return (kira_int)(intptr_t)v;\n");
        try self.write("}\n");
        try self.write("/* Make Err(msg) variant: tag=1, payload at [1] */\n");
        try self.write("static kira_int kira_make_err(kira_string msg) {\n");
        try self.write("    kira_int* v = (kira_int*)KIRA_ALLOC(2 * sizeof(kira_int));\n");
        try self.write("    v[0] = 1; v[1] = (kira_int)(intptr_t)msg;\n");
        try self.write("    return (kira_int)(intptr_t)v;\n");
        try self.write("}\n\n");

        // fs_read_file(path) -> Result[string, string]
        try self.write("static kira_int kira_fs_read_file(kira_string path) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_read_file\");\n");
        try self.write("    FILE* f = fopen(path, \"rb\");\n");
        try self.write("    if (!f) return kira_make_err(\"FileNotFound\");\n");
        try self.write("    fseek(f, 0, SEEK_END);\n");
        try self.write("    long len = ftell(f);\n");
        try self.write("    fseek(f, 0, SEEK_SET);\n");
        try self.write("    char* buf = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    size_t read = fread(buf, 1, len, f);\n");
        try self.write("    fclose(f);\n");
        try self.write("    buf[read] = '\\0';\n");
        try self.write("    return kira_make_ok((kira_int)(intptr_t)buf);\n");
        try self.write("}\n\n");

        // fs_write_file(path, contents) -> Result[void, string]
        try self.write("static kira_int kira_fs_write_file(kira_string path, kira_string contents) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_write_file\");\n");
        try self.write("    FILE* f = fopen(path, \"wb\");\n");
        try self.write("    if (!f) return kira_make_err(\"FileNotFound\");\n");
        try self.write("    size_t len = strlen(contents);\n");
        try self.write("    size_t written = fwrite(contents, 1, len, f);\n");
        try self.write("    fclose(f);\n");
        try self.write("    if (written != len) return kira_make_err(\"WriteError\");\n");
        try self.write("    return kira_make_ok(0);\n");
        try self.write("}\n\n");

        // fs_exists(path) -> bool
        try self.write("static kira_int kira_fs_exists(kira_string path) {\n");
        try self.write("    return access(path, F_OK) == 0 ? 1 : 0;\n");
        try self.write("}\n\n");

        // fs_remove(path) -> Result[void, string]
        try self.write("static kira_int kira_fs_remove(kira_string path) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_remove\");\n");
        try self.write("    if (unlink(path) == 0) return kira_make_ok(0);\n");
        try self.write("    return kira_make_err(\"RemoveError\");\n");
        try self.write("}\n\n");

        // fs_append_file(path, contents) -> Result[void, string]
        try self.write("static kira_int kira_fs_append_file(kira_string path, kira_string contents) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_append_file\");\n");
        try self.write("    FILE* f = fopen(path, \"ab\");\n");
        try self.write("    if (!f) return kira_make_err(\"FileNotFound\");\n");
        try self.write("    size_t len = strlen(contents);\n");
        try self.write("    size_t written = fwrite(contents, 1, len, f);\n");
        try self.write("    fclose(f);\n");
        try self.write("    if (written != len) return kira_make_err(\"WriteError\");\n");
        try self.write("    return kira_make_ok(0);\n");
        try self.write("}\n\n");

        // --- Tier 5: Time helpers ---
        // time_now() -> i64 (milliseconds since epoch)
        try self.write("static kira_int kira_time_now(void) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"time_now\");\n");
        try self.write("    struct timeval tv;\n");
        try self.write("    gettimeofday(&tv, NULL);\n");
        try self.write("    return (kira_int)(tv.tv_sec * 1000LL + tv.tv_usec / 1000);\n");
        try self.write("}\n\n");

        // time_sleep(ms) -> void
        try self.write("static void kira_time_sleep(kira_int ms) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"time_sleep\");\n");
        try self.write("    usleep((useconds_t)(ms * 1000));\n");
        try self.write("}\n\n");

        // Command-line argument globals (set by main(), read by kira_env_args())
        try self.write("/* Command-line arguments */\n");
        try self.write("static int _kira_argc = 0;\n");
        try self.write("static char** _kira_argv = NULL;\n");
        try self.write("static kira_int kira_env_args(void) {\n");
        try self.write("    kira_int* arr = (kira_int*)KIRA_ALLOC((_kira_argc + 1) * sizeof(kira_int));\n");
        try self.write("    arr[0] = _kira_argc;\n");
        try self.write("    for (int i = 0; i < _kira_argc; i++) {\n");
        try self.write("        arr[i + 1] = (kira_int)(intptr_t)_kira_argv[i];\n");
        try self.write("    }\n");
        try self.write("    return (kira_int)(intptr_t)arr;\n");
        try self.write("}\n\n");

        // List operations (operate on Kira arrays: [length, elem0, elem1, ...])
        try self.write("/* List operations */\n");
        try self.write("typedef kira_int (*_kira_fn1)(kira_int, kira_int);\n");
        // list_length: return the length field of a Kira array
        try self.write("static kira_int kira_list_length(kira_int arr_val) {\n");
        try self.write("    kira_int* arr = (kira_int*)(intptr_t)arr_val;\n");
        try self.write("    return arr[0];\n");
        try self.write("}\n");
        // list_map: apply a closure to each element, return new array
        try self.write("static kira_int kira_list_map(kira_int arr_val, kira_int fn_val) {\n");
        try self.write("    kira_int* arr = (kira_int*)(intptr_t)arr_val;\n");
        try self.write("    kira_int len = arr[0];\n");
        try self.write("    kira_int* result = (kira_int*)KIRA_ALLOC((len + 1) * sizeof(kira_int));\n");
        try self.write("    result[0] = len;\n");
        try self.write("    kira_int* closure = (kira_int*)(intptr_t)fn_val;\n");
        try self.write("    _kira_fn1 fn_ptr = (_kira_fn1)(intptr_t)closure[0];\n");
        try self.write("    for (kira_int i = 0; i < len; i++) {\n");
        try self.write("        result[i + 1] = fn_ptr(fn_val, arr[i + 1]);\n");
        try self.write("    }\n");
        try self.write("    return (kira_int)(intptr_t)result;\n");
        try self.write("}\n");
        // list_filter: keep elements where predicate closure returns true
        try self.write("static kira_int kira_list_filter(kira_int arr_val, kira_int fn_val) {\n");
        try self.write("    kira_int* arr = (kira_int*)(intptr_t)arr_val;\n");
        try self.write("    kira_int len = arr[0];\n");
        try self.write("    kira_int* tmp = (kira_int*)KIRA_ALLOC((len + 1) * sizeof(kira_int));\n");
        try self.write("    kira_int count = 0;\n");
        try self.write("    kira_int* closure = (kira_int*)(intptr_t)fn_val;\n");
        try self.write("    _kira_fn1 fn_ptr = (_kira_fn1)(intptr_t)closure[0];\n");
        try self.write("    for (kira_int i = 0; i < len; i++) {\n");
        try self.write("        if (fn_ptr(fn_val, arr[i + 1])) { tmp[count + 1] = arr[i + 1]; count++; }\n");
        try self.write("    }\n");
        try self.write("    tmp[0] = count;\n");
        try self.write("    return (kira_int)(intptr_t)tmp;\n");
        try self.write("}\n\n");

        // --- Additional list operations ---
        try self.write("static kira_int kira_list_empty(void) {\n");
        try self.write("    kira_int* arr = (kira_int*)KIRA_ALLOC(1 * sizeof(kira_int));\n");
        try self.write("    arr[0] = 0;\n");
        try self.write("    return (kira_int)(intptr_t)arr;\n");
        try self.write("}\n");
        try self.write("static kira_int kira_list_singleton(kira_int val) {\n");
        try self.write("    kira_int* arr = (kira_int*)KIRA_ALLOC(2 * sizeof(kira_int));\n");
        try self.write("    arr[0] = 1; arr[1] = val;\n");
        try self.write("    return (kira_int)(intptr_t)arr;\n");
