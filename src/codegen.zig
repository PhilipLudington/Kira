    try self.writeFmt(" else {{ kira_int* _opt = (kira_int*)KIRA_ALLOC(1 * sizeof(kira_int)); _opt[0] = 0; v{d} = (kira_int)(intptr_t)_opt; }} }}\n", .{ref});
