test "codegen arithmetic expression" {
    const backing = std.testing.allocator;
    var module = Module.init(backing);
    defer module.deinit();
    const alloc = module.allocator();

    // fn add() { return 10 + 20; }
    var func = Function.init(alloc);
    func.name = "add";
    const blk = try func.addBlock(alloc);
    const a = try func.addInstruction(alloc, blk, .{ .op = .{ .const_int = .{ .value = 10 } } });
    const b = try func.addInstruction(alloc, blk, .{ .op = .{ .const_int = .{ .value = 20 } } });
    const c = try func.addInstruction(alloc, blk, .{ .op = .{ .int_binop = .{ .op = .add, .left = a, .right = b } } });
    func.setTerminator(blk, .{ .ret = c });
    _ = try module.addFunction(func);

    var gen = CCodeGen.init(backing);
    defer gen.deinit();
    try gen.generateModule(&module);

    const output = gen.getOutput();
    try std.testing.expect(std.mem.indexOf(u8, output, "kira_add") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "v0 = 10;") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "v1 = 20;") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "v2 = v0 + v1;") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "return v2;") != null);
}

test "codegen function call" {
    const backing = std.testing.allocator;
    var module = Module.init(backing);
    defer module.deinit();
    const alloc = module.allocator();

    // fn caller(f: i64) { return f(42); }
    const params = [_]Function.Param{.{ .name = "f", .value_ref = 0 }};
    var func = Function.init(alloc);
    func.name = "caller";
    func.params = &params;
    const blk = try func.addBlock(alloc);
    _ = try func.addInstruction(alloc, blk, .{ .op = .{ .param = 0 } });
    const arg = try func.addInstruction(alloc, blk, .{ .op = .{ .const_int = .{ .value = 42 } } });
