//! C code generation backend for Kira.
//!
//! Translates Kira IR into C source code that can be compiled with any
//! standard C compiler (gcc, clang).
//!
//! Each IR function becomes a C function. SSA values become local variables.
//! Basic blocks become labeled sections with goto-based control flow.
//!
//! Type-specific code generation:
//! - Function signatures use proper C types (int32_t, double, bool, etc.)
//!   derived from IR param type_name and return_type_name
//! - Record struct fields use proper C types from FieldDecl.type_name
//! - Constants use type-appropriate C declarations
//! - SSA variables use uniform kira_int internally for compound type compat
//!
//! Compound types are heap-allocated and referenced via intptr_t casts:
//! - Tuples: kira_int* with elements at [0..N)
//! - Arrays: kira_int* with length at [0], elements at [1..N]
//! - Records: kira_int* with fields at [0..N) indexed by declaration order
//! - Variants: kira_int* with tag at [0], payload fields at [1..N]
//! - Closures: kira_int* with function pointer at [0], captures at [1..N]
//! - Strings: char* cast through intptr_t; concat via malloc+strcat

const std = @import("std");
const Allocator = std.mem.Allocator;
const ir = @import("ir/ir.zig");
const Module = ir.Module;
const Function = ir.Function;
const Instruction = ir.Instruction;
const Terminator = ir.Terminator;
const ValueRef = ir.ValueRef;
const BlockId = ir.BlockId;

pub const CodeGenError = error{
    OutOfMemory,
    UnsupportedInstruction,
    InvalidIR,
};

/// C code generator that emits C source from Kira IR.
pub const CCodeGen = struct {
    allocator: Allocator,
    /// Output buffer for generated C code.
    output: std.ArrayListUnmanaged(u8),
    /// Current indentation level.
    indent_level: u32,
    /// Reference to the module being generated (set during generateModule).
    current_module: ?*const Module,
    /// SSA values known to hold string values (for string comparison via strcmp).
    string_values: std.AutoArrayHashMapUnmanaged(ValueRef, void),

    pub fn init(allocator: Allocator) CCodeGen {
        return .{
            .allocator = allocator,
            .output = .{},
            .indent_level = 0,
            .current_module = null,
            .string_values = .{},
        };
    }

    pub fn deinit(self: *CCodeGen) void {
        self.output.deinit(self.allocator);
        self.string_values.deinit(self.allocator);
    }

    /// Get the generated C source code.
    pub fn getOutput(self: *const CCodeGen) []const u8 {
        return self.output.items;
    }

    /// Generate C code for an entire IR module.
    pub fn generateModule(self: *CCodeGen, module: *const Module) CodeGenError!void {
        self.current_module = module;
        try self.emitPreamble();
        try self.emitTypeDecls(module);
        try self.emitFunctionDeclarations(module);
        try self.emitConstants(module);
        for (module.functions.items) |*func| {
            try self.emitFunction(func);
        }
        try self.emitEntryPoint(module);
    }

    /// Emit a C main() entry point that calls kira_main().
    fn emitEntryPoint(self: *CCodeGen, module: *const Module) CodeGenError!void {
        // Check if there's a kira main function
        if (module.function_map.get("main")) |idx| {
            const func = &module.functions.items[idx];
            const has_return = !std.mem.eql(u8, func.return_type_name, "void");

            try self.write("int main(int argc, char** argv) {\n");
            self.indent_level += 1;
            try self.writeIndent();
            try self.write("KIRA_GC_INIT();\n");
            try self.writeIndent();
            try self.write("_kira_argc = argc;\n");
            try self.writeIndent();
            try self.write("_kira_argv = argv;\n");
            try self.writeIndent();
            if (has_return) {
                try self.writeFmt("kira_int result = kira_main();\n", .{});
                try self.writeIndent();
                try self.write("printf(\"%lld\\n\", (long long)result);\n");
                try self.writeIndent();
                try self.write("return 0;\n");
            } else {
                try self.write("kira_main();\n");
                try self.writeIndent();
                try self.write("return 0;\n");
            }
            self.indent_level -= 1;
            try self.write("}\n");
        }
    }

    // ----------------------------------------------------------------
    // Preamble
    // ----------------------------------------------------------------

    fn emitPreamble(self: *CCodeGen) CodeGenError!void {
        try self.write("/* Generated by Kira compiler */\n");
        try self.write("#include <stdint.h>\n");
        try self.write("#include <stdbool.h>\n");
        try self.write("#include <stdio.h>\n");
        try self.write("#include <stdlib.h>\n");
        try self.write("#include <string.h>\n");
        try self.write("#include <math.h>\n\n");
        // GC abstraction: compile with -DKIRA_NO_GC to use plain malloc
        try self.write("/* GC configuration */\n");
        try self.write("#ifdef KIRA_NO_GC\n");
        try self.write("#define KIRA_ALLOC(sz) malloc(sz)\n");
        try self.write("#define KIRA_GC_INIT() ((void)0)\n");
        try self.write("#else\n");
        try self.write("#include <gc.h>\n");
        try self.write("#define KIRA_ALLOC(sz) GC_MALLOC(sz)\n");
        try self.write("#define KIRA_GC_INIT() GC_INIT()\n");
        try self.write("#endif\n\n");
        try self.write("/* Kira runtime types */\n");
        try self.write("typedef int64_t kira_int;\n");
        try self.write("typedef double kira_float;\n");
        try self.write("typedef bool kira_bool;\n");
        try self.write("typedef uint32_t kira_char;\n");
        try self.write("typedef const char* kira_string;\n");
        try self.write("typedef void kira_void;\n\n");

        // Effect tracking system
        try self.write("/* Kira effect tracking */\n");
        try self.write("#ifdef KIRA_DEBUG_EFFECTS\n");
        try self.write("static int _kira_pure_depth = 0;\n");
        try self.write("#define KIRA_ENTER_PURE() (_kira_pure_depth++)\n");
        try self.write("#define KIRA_LEAVE_PURE() (_kira_pure_depth--)\n");
        try self.write("#define KIRA_ASSERT_EFFECT(fn_name) do { \\\n");
        try self.write("    if (_kira_pure_depth > 0) { \\\n");
        try self.write("        fprintf(stderr, \"Kira effect violation: %s called from pure context\\n\", fn_name); \\\n");
        try self.write("        abort(); \\\n");
        try self.write("    } \\\n");
        try self.write("} while(0)\n");
        try self.write("#else\n");
        try self.write("#define KIRA_ENTER_PURE() ((void)0)\n");
        try self.write("#define KIRA_LEAVE_PURE() ((void)0)\n");
        try self.write("#define KIRA_ASSERT_EFFECT(fn_name) ((void)0)\n");
        try self.write("#endif\n\n");

        try self.write("/* Kira runtime IO functions (effect-only) */\n");
        try self.write("static void kira_println(kira_string s) { KIRA_ASSERT_EFFECT(\"println\"); printf(\"%s\\n\", s); }\n");
        try self.write("static void kira_print(kira_string s) { KIRA_ASSERT_EFFECT(\"print\"); printf(\"%s\", s); }\n");
        try self.write("static void kira_println_int(kira_int n) { KIRA_ASSERT_EFFECT(\"println_int\"); printf(\"%lld\\n\", (long long)n); }\n");
        try self.write("static void kira_print_int(kira_int n) { KIRA_ASSERT_EFFECT(\"print_int\"); printf(\"%lld\", (long long)n); }\n");
        try self.write("static kira_string kira_int_to_string(kira_int n) {\n");
        try self.write("    char* s = (char*)KIRA_ALLOC(32);\n");
        try self.write("    snprintf(s, 32, \"%lld\", (long long)n);\n");
        try self.write("    return s;\n");
        try self.write("}\n\n");

        // String helper functions
        try self.write("#include <ctype.h>\n");
        try self.write("static kira_string kira_string_trim(kira_string s) {\n");
        try self.write("    while (*s && isspace((unsigned char)*s)) s++;\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    while (len > 0 && isspace((unsigned char)s[len-1])) len--;\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    memcpy(r, s, len); r[len] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_int kira_string_split(kira_string str, kira_string delim) {\n");
        try self.write("    size_t dlen = strlen(delim);\n");
        try self.write("    size_t count = 1;\n");
        try self.write("    const char* p = str;\n");
        try self.write("    if (dlen > 0) { while ((p = strstr(p, delim)) != NULL) { count++; p += dlen; } }\n");
        try self.write("    kira_int* arr = (kira_int*)KIRA_ALLOC((count + 1) * sizeof(kira_int));\n");
        try self.write("    arr[0] = (kira_int)count;\n");
        try self.write("    p = str;\n");
        try self.write("    for (size_t i = 0; i < count; i++) {\n");
        try self.write("        const char* next = (dlen > 0) ? strstr(p, delim) : NULL;\n");
        try self.write("        size_t slen = next ? (size_t)(next - p) : strlen(p);\n");
        try self.write("        char* seg = (char*)KIRA_ALLOC(slen + 1);\n");
        try self.write("        memcpy(seg, p, slen); seg[slen] = '\\0';\n");
        try self.write("        arr[i + 1] = (kira_int)(intptr_t)seg;\n");
        try self.write("        p = next ? next + dlen : p + slen;\n");
        try self.write("    }\n");
        try self.write("    return (kira_int)(intptr_t)arr;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_substring(kira_string s, kira_int start, kira_int end) {\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    if (start < 0) start = 0;\n");
        try self.write("    if ((size_t)end > len) end = (kira_int)len;\n");
        try self.write("    if (start >= end) { char* r = (char*)KIRA_ALLOC(1); r[0] = '\\0'; return r; }\n");
        try self.write("    size_t slen = (size_t)(end - start);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(slen + 1);\n");
        try self.write("    memcpy(r, s + start, slen); r[slen] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_to_upper(kira_string s) {\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    for (size_t i = 0; i < len; i++) r[i] = (char)toupper((unsigned char)s[i]);\n");
        try self.write("    r[len] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_to_lower(kira_string s) {\n");
        try self.write("    size_t len = strlen(s);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    for (size_t i = 0; i < len; i++) r[i] = (char)tolower((unsigned char)s[i]);\n");
        try self.write("    r[len] = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n");

        try self.write("static kira_string kira_string_replace(kira_string s, kira_string old, kira_string new_s) {\n");
        try self.write("    size_t olen = strlen(old), nlen = strlen(new_s), slen = strlen(s);\n");
        try self.write("    if (olen == 0) { char* r = (char*)KIRA_ALLOC(slen+1); memcpy(r,s,slen+1); return r; }\n");
        try self.write("    size_t count = 0;\n");
        try self.write("    const char* p = s;\n");
        try self.write("    while ((p = strstr(p, old)) != NULL) { count++; p += olen; }\n");
        try self.write("    size_t rlen = slen + count * (nlen - olen);\n");
        try self.write("    char* r = (char*)KIRA_ALLOC(rlen + 1);\n");
        try self.write("    char* w = r; p = s;\n");
        try self.write("    while (*p) {\n");
        try self.write("        if (strncmp(p, old, olen) == 0) { memcpy(w, new_s, nlen); w += nlen; p += olen; }\n");
        try self.write("        else { *w++ = *p++; }\n");
        try self.write("    }\n");
        try self.write("    *w = '\\0';\n");
        try self.write("    return r;\n");
        try self.write("}\n\n");

        // --- Tier 5: Filesystem helpers ---
        try self.write("#include <unistd.h>\n");
        try self.write("#include <sys/time.h>\n\n");

        // Result variant encoding: Ok=tag 0, Err=tag 1
        // Layout: kira_int* [tag][payload]
        try self.write("/* Make Ok(value) variant: tag=0, payload at [1] */\n");
        try self.write("static kira_int kira_make_ok(kira_int value) {\n");
        try self.write("    kira_int* v = (kira_int*)KIRA_ALLOC(2 * sizeof(kira_int));\n");
        try self.write("    v[0] = 0; v[1] = value;\n");
        try self.write("    return (kira_int)(intptr_t)v;\n");
        try self.write("}\n");
        try self.write("/* Make Err(msg) variant: tag=1, payload at [1] */\n");
        try self.write("static kira_int kira_make_err(kira_string msg) {\n");
        try self.write("    kira_int* v = (kira_int*)KIRA_ALLOC(2 * sizeof(kira_int));\n");
        try self.write("    v[0] = 1; v[1] = (kira_int)(intptr_t)msg;\n");
        try self.write("    return (kira_int)(intptr_t)v;\n");
        try self.write("}\n\n");

        // fs_read_file(path) -> Result[string, string]
        try self.write("static kira_int kira_fs_read_file(kira_string path) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_read_file\");\n");
        try self.write("    FILE* f = fopen(path, \"rb\");\n");
        try self.write("    if (!f) return kira_make_err(\"FileNotFound\");\n");
        try self.write("    fseek(f, 0, SEEK_END);\n");
        try self.write("    long len = ftell(f);\n");
        try self.write("    fseek(f, 0, SEEK_SET);\n");
        try self.write("    char* buf = (char*)KIRA_ALLOC(len + 1);\n");
        try self.write("    size_t read = fread(buf, 1, len, f);\n");
        try self.write("    fclose(f);\n");
        try self.write("    buf[read] = '\\0';\n");
        try self.write("    return kira_make_ok((kira_int)(intptr_t)buf);\n");
        try self.write("}\n\n");

        // fs_write_file(path, contents) -> Result[void, string]
        try self.write("static kira_int kira_fs_write_file(kira_string path, kira_string contents) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_write_file\");\n");
        try self.write("    FILE* f = fopen(path, \"wb\");\n");
        try self.write("    if (!f) return kira_make_err(\"FileNotFound\");\n");
        try self.write("    size_t len = strlen(contents);\n");
        try self.write("    size_t written = fwrite(contents, 1, len, f);\n");
        try self.write("    fclose(f);\n");
        try self.write("    if (written != len) return kira_make_err(\"WriteError\");\n");
        try self.write("    return kira_make_ok(0);\n");
        try self.write("}\n\n");

        // fs_exists(path) -> bool
        try self.write("static kira_int kira_fs_exists(kira_string path) {\n");
        try self.write("    return access(path, F_OK) == 0 ? 1 : 0;\n");
        try self.write("}\n\n");

        // fs_remove(path) -> Result[void, string]
        try self.write("static kira_int kira_fs_remove(kira_string path) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_remove\");\n");
        try self.write("    if (unlink(path) == 0) return kira_make_ok(0);\n");
        try self.write("    return kira_make_err(\"RemoveError\");\n");
        try self.write("}\n\n");

        // fs_append_file(path, contents) -> Result[void, string]
        try self.write("static kira_int kira_fs_append_file(kira_string path, kira_string contents) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"fs_append_file\");\n");
        try self.write("    FILE* f = fopen(path, \"ab\");\n");
        try self.write("    if (!f) return kira_make_err(\"FileNotFound\");\n");
        try self.write("    size_t len = strlen(contents);\n");
        try self.write("    size_t written = fwrite(contents, 1, len, f);\n");
        try self.write("    fclose(f);\n");
        try self.write("    if (written != len) return kira_make_err(\"WriteError\");\n");
        try self.write("    return kira_make_ok(0);\n");
        try self.write("}\n\n");

        // --- Tier 5: Time helpers ---
        // time_now() -> i64 (milliseconds since epoch)
        try self.write("static kira_int kira_time_now(void) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"time_now\");\n");
        try self.write("    struct timeval tv;\n");
        try self.write("    gettimeofday(&tv, NULL);\n");
        try self.write("    return (kira_int)(tv.tv_sec * 1000LL + tv.tv_usec / 1000);\n");
        try self.write("}\n\n");

        // time_sleep(ms) -> void
        try self.write("static void kira_time_sleep(kira_int ms) {\n");
        try self.write("    KIRA_ASSERT_EFFECT(\"time_sleep\");\n");
        try self.write("    usleep((useconds_t)(ms * 1000));\n");
        try self.write("}\n\n");

        // Command-line argument globals (set by main(), read by kira_env_args())
        try self.write("/* Command-line arguments */\n");
        try self.write("static int _kira_argc = 0;\n");
        try self.write("static char** _kira_argv = NULL;\n");
        try self.write("static kira_int kira_env_args(void) {\n");
        try self.write("    kira_int* arr = (kira_int*)KIRA_ALLOC((_kira_argc + 1) * sizeof(kira_int));\n");
        try self.write("    arr[0] = _kira_argc;\n");
        try self.write("    for (int i = 0; i < _kira_argc; i++) {\n");
        try self.write("        arr[i + 1] = (kira_int)(intptr_t)_kira_argv[i];\n");
        try self.write("    }\n");
        try self.write("    return (kira_int)(intptr_t)arr;\n");
        try self.write("}\n\n");

        // Memoization cache support (general case: linked-list cache)
        try self.write("/* Memoization cache */\n");
        try self.write("#define KIRA_MEMO_ARRAY_SIZE 4096\n");
        try self.write("#define KIRA_MEMO_LIST_MAX 4096\n");
        try self.write("typedef struct _KiraMemoEntry {\n");
        try self.write("    kira_int* args;\n");
        try self.write("    int arg_count;\n");
        try self.write("    kira_int result;\n");
        try self.write("    struct _KiraMemoEntry* next;\n");
        try self.write("} _KiraMemoEntry;\n");
        try self.write("typedef struct {\n");
        try self.write("    _KiraMemoEntry* head;\n");
        try self.write("    int count;\n");
        try self.write("} _KiraMemoCache;\n");
        try self.write("static kira_int _kira_memo_lookup(_KiraMemoCache* cache, kira_int* args, int arg_count, bool* found) {\n");
        try self.write("    for (_KiraMemoEntry* e = cache->head; e; e = e->next) {\n");
        try self.write("        if (e->arg_count == arg_count && memcmp(e->args, args, arg_count * sizeof(kira_int)) == 0) {\n");
        try self.write("            *found = true;\n");
        try self.write("            return e->result;\n");
        try self.write("        }\n");
        try self.write("    }\n");
        try self.write("    *found = false;\n");
        try self.write("    return 0;\n");
        try self.write("}\n");
        try self.write("static void _kira_memo_store(_KiraMemoCache* cache, kira_int* args, int arg_count, kira_int result) {\n");
        try self.write("    if (cache->count >= KIRA_MEMO_LIST_MAX) return;\n");
        try self.write("    _KiraMemoEntry* e = (_KiraMemoEntry*)KIRA_ALLOC(sizeof(_KiraMemoEntry));\n");
        try self.write("    e->args = (kira_int*)KIRA_ALLOC(arg_count * sizeof(kira_int));\n");
        try self.write("    memcpy(e->args, args, arg_count * sizeof(kira_int));\n");
        try self.write("    e->arg_count = arg_count;\n");
        try self.write("    e->result = result;\n");
        try self.write("    e->next = cache->head;\n");
        try self.write("    cache->head = e;\n");
        try self.write("    cache->count++;\n");
        try self.write("}\n\n");
    }

    // ----------------------------------------------------------------
    // Type declarations
    // ----------------------------------------------------------------

    fn emitTypeDecls(self: *CCodeGen, module: *const Module) CodeGenError!void {
        if (module.type_decls.items.len == 0) return;
        try self.write("/* Type declarations */\n");
        for (module.type_decls.items) |td| {
            switch (td.kind) {
                .sum_type => |st| {
                    try self.write("/* Sum type: ");
                    try self.write(td.name);
                    try self.write(" */\n");
                    try self.write("typedef struct {\n");
                    self.indent_level += 1;
                    try self.writeIndent();
                    try self.write("uint32_t tag;\n");
                    // Find max field count across variants
                    var max_fields: u32 = 0;
                    for (st.variants) |v| {
                        if (v.field_count > max_fields) max_fields = v.field_count;
                    }
                    if (max_fields > 0) {
                        try self.writeIndent();
                        try self.writeFmt("kira_int fields[{d}];\n", .{max_fields});
                    }
                    self.indent_level -= 1;
                    try self.write("} kira_");
                    try self.write(td.name);
                    try self.write(";\n\n");

                    // Emit tag constants
                    for (st.variants) |v| {
                        try self.writeFmt("#define TAG_{s}_{s} {d}\n", .{ td.name, v.name, v.tag });
                    }
                    try self.write("\n");
                },
                .product_type => |pt| {
                    try self.write("/* Record type: ");
                    try self.write(td.name);
                    try self.write(" */\n");
                    try self.write("typedef struct {\n");
                    self.indent_level += 1;
                    for (pt.fields) |f| {
                        try self.writeIndent();
                        try self.write(kiraTypeToCType(f.type_name));
                        try self.writeFmt(" {s};\n", .{f.name});
                    }
                    self.indent_level -= 1;
                    try self.write("} kira_");
                    try self.write(td.name);
                    try self.write(";\n\n");
                },
            }
        }
    }

    // ----------------------------------------------------------------
    // Forward declarations
    // ----------------------------------------------------------------

    fn emitFunctionDeclarations(self: *CCodeGen, module: *const Module) CodeGenError!void {
        if (module.functions.items.len == 0) return;
        try self.write("/* Forward declarations */\n");
        for (module.functions.items) |*func| {
            if (func.is_effect) {
                try self.write("/* effect */ ");
            } else {
                try self.write("/* pure */ ");
            }
            if (func.is_memoized) {
                try self.write("/* memo */ ");
            }
            try self.write(kiraTypeToCType(func.return_type_name));
            try self.write(" ");
            try self.emitFunctionName(func);
            try self.write("(");
            for (func.params, 0..) |p, i| {
                if (i > 0) try self.write(", ");
                try self.write(kiraTypeToCType(p.type_name));
                try self.writeFmt(" {s}", .{sanitizeName(p.name)});
            }
            if (func.params.len == 0) try self.write("void");
            try self.write(");\n");

            // For memoized functions, also forward-declare the _memo_impl version
            if (func.is_memoized) {
                try self.write("static ");
                try self.write(kiraTypeToCType(func.return_type_name));
                try self.write(" ");
                try self.emitFunctionName(func);
                try self.write("_memo_impl(");
                for (func.params, 0..) |p, i| {
                    if (i > 0) try self.write(", ");
                    try self.write(kiraTypeToCType(p.type_name));
                    try self.writeFmt(" {s}", .{sanitizeName(p.name)});
                }
                if (func.params.len == 0) try self.write("void");
                try self.write(");\n");
            }
        }
        try self.write("\n");
    }

    // ----------------------------------------------------------------
    // Constants
    // ----------------------------------------------------------------

    fn emitConstants(self: *CCodeGen, module: *const Module) CodeGenError!void {
        if (module.constants.items.len == 0) return;
        try self.write("/* Constants */\n");
        for (module.constants.items) |c| {
            switch (c.value) {
                .integer => |v| try self.writeFmt("static const int64_t kira_const_{s} = {d};\n", .{ c.name, v }),
                .float => |v| try self.writeFmt("static const double kira_const_{s} = {d};\n", .{ c.name, v }),
                .boolean => |b| try self.writeFmt("static const bool kira_const_{s} = {s};\n", .{ c.name, if (b) "true" else "false" }),
                .string => |s| {
                    try self.writeFmt("static const char* kira_const_{s} = \"", .{c.name});
                    try self.write(s);
                    try self.write("\";\n");
                },
                .char => |c_val| try self.writeFmt("static const uint32_t kira_const_{s} = {d};\n", .{ c.name, c_val }),
                .void_val => try self.writeFmt("static const int64_t kira_const_{s} = 0;\n", .{c.name}),
            }
        }
        try self.write("\n");
    }

    // ----------------------------------------------------------------
    // Function emission
    // ----------------------------------------------------------------

    fn emitFunction(self: *CCodeGen, func: *const Function) CodeGenError!void {
        // Clear per-function string tracking
        self.string_values.clearRetainingCapacity();
        // Pre-scan: mark string-typed params
        for (func.params) |p| {
            if (std.mem.eql(u8, p.type_name, "string")) {
                self.string_values.put(self.allocator, p.value_ref, {}) catch {};
            }
        }
        // Pre-scan: mark string-producing instructions
        for (func.instructions.items, 0..) |inst, idx| {
            const ref: ValueRef = @intCast(idx);
            switch (inst.op) {
                .const_string => self.string_values.put(self.allocator, ref, {}) catch {},
                .str_concat => self.string_values.put(self.allocator, ref, {}) catch {},
                .call_builtin => |cb| {
                    if (std.mem.eql(u8, cb.name, "int_to_string") or
                        std.mem.eql(u8, cb.name, "float_to_string") or
                        std.mem.eql(u8, cb.name, "string_trim") or
                        std.mem.eql(u8, cb.name, "string_substring") or
                        std.mem.eql(u8, cb.name, "string_to_upper") or
                        std.mem.eql(u8, cb.name, "string_to_lower") or
                        std.mem.eql(u8, cb.name, "string_replace") or
                        std.mem.eql(u8, cb.name, "read_line"))
                    {
                        self.string_values.put(self.allocator, ref, {}) catch {};
                    }
                },
                else => {},
            }
        }

        // Function signature with effect annotation
        if (func.is_effect) {
            try self.write("/* effect */ ");
        } else {
            try self.write("/* pure */ ");
        }
        if (func.is_memoized) {
            try self.write("/* memo impl */ ");
        }
        try self.write(kiraTypeToCType(func.return_type_name));
        try self.write(" ");
        try self.emitFunctionName(func);
        if (func.is_memoized) try self.write("_memo_impl");
        try self.write("(");
        for (func.params, 0..) |p, i| {
            if (i > 0) try self.write(", ");
            try self.write(kiraTypeToCType(p.type_name));
            try self.writeFmt(" {s}", .{sanitizeName(p.name)});
        }
        if (func.params.len == 0) try self.write("void");
        try self.write(") {\n");
        self.indent_level += 1;

        // Effect tracking: pure functions enter pure context
        if (!func.is_effect) {
            try self.writeIndent();
            try self.write("KIRA_ENTER_PURE();\n");
        }

        // Declare all SSA variables
        if (func.instructions.items.len > 0) {
            try self.writeIndent();
            try self.write("kira_int ");
            for (0..func.instructions.items.len) |i| {
                if (i > 0) try self.write(", ");
                try self.writeFmt("v{d}", .{i});
            }
            try self.write(";\n");
        }

        // Emit basic blocks
        for (func.blocks.items) |*blk| {
            try self.emitBlock(func, blk);
        }

        self.indent_level -= 1;
        try self.write("}\n\n");

        // For memoized functions, emit the caching wrapper
        if (func.is_memoized) {
            try self.emitMemoWrapper(func);
        }
    }

    /// Emit a memoization caching wrapper for a memoized function.
    /// The wrapper checks a cache before delegating to the _memo_impl function.
    fn emitMemoWrapper(self: *CCodeGen, func: *const Function) CodeGenError!void {
        const ret_type = kiraTypeToCType(func.return_type_name);

        // Determine if we can use the fast array cache (single int-like param)
        const use_array_cache = func.params.len == 1 and isIntLikeType(func.params[0].type_name);

        try self.write("/* memo wrapper */ ");
        try self.write(ret_type);
        try self.write(" ");
        try self.emitFunctionName(func);
        try self.write("(");
        for (func.params, 0..) |p, i| {
            if (i > 0) try self.write(", ");
            try self.write(kiraTypeToCType(p.type_name));
            try self.writeFmt(" {s}", .{sanitizeName(p.name)});
        }
        if (func.params.len == 0) try self.write("void");
        try self.write(") {\n");
        self.indent_level += 1;

        if (use_array_cache) {
            // Fast path: single int param → static array cache
            try self.writeIndent();
            try self.writeFmt("static {s} _cache[KIRA_MEMO_ARRAY_SIZE];\n", .{ret_type});
            try self.writeIndent();
            try self.write("static bool _set[KIRA_MEMO_ARRAY_SIZE] = {0};\n");

            const param_name = sanitizeName(func.params[0].name);
            // Cache lookup
            try self.writeIndent();
            try self.writeFmt("if ({s} >= 0 && {s} < KIRA_MEMO_ARRAY_SIZE && _set[{s}]) return _cache[{s}];\n", .{ param_name, param_name, param_name, param_name });

            // Call impl
            try self.writeIndent();
            try self.writeFmt("{s} _result = ", .{ret_type});
            try self.emitFunctionName(func);
            try self.writeFmt("_memo_impl({s});\n", .{param_name});

            // Store in cache
            try self.writeIndent();
            try self.writeFmt("if ({s} >= 0 && {s} < KIRA_MEMO_ARRAY_SIZE) {{ _cache[{s}] = _result; _set[{s}] = true; }}\n", .{ param_name, param_name, param_name, param_name });
        } else {
            // General path: linked-list cache
            try self.writeIndent();
            try self.write("static _KiraMemoCache _cache = {0};\n");
            try self.writeIndent();
            try self.writeFmt("kira_int _args[{d}]", .{@max(func.params.len, 1)});
            if (func.params.len == 0) {
                try self.write(" = {0};\n");
            } else {
                try self.write(";\n");
                for (func.params, 0..) |p, i| {
                    try self.writeIndent();
                    try self.writeFmt("_args[{d}] = (kira_int){s};\n", .{ i, sanitizeName(p.name) });
                }
            }

            // Cache lookup
            try self.writeIndent();
            try self.write("bool _found = false;\n");
            try self.writeIndent();
            try self.writeFmt("kira_int _cached = _kira_memo_lookup(&_cache, _args, {d}, &_found);\n", .{func.params.len});
            try self.writeIndent();
            if (std.mem.eql(u8, ret_type, "const char*")) {
                try self.write("if (_found) return (const char*)(intptr_t)_cached;\n");
            } else if (std.mem.eql(u8, ret_type, "void")) {
                try self.write("if (_found) return;\n");
            } else {
                try self.writeFmt("if (_found) return ({s})_cached;\n", .{ret_type});
            }

            // Call impl
            try self.writeIndent();
            if (std.mem.eql(u8, ret_type, "void")) {
                try self.emitFunctionName(func);
                try self.write("_memo_impl(");
            } else {
                try self.writeFmt("{s} _result = ", .{ret_type});
                try self.emitFunctionName(func);
                try self.write("_memo_impl(");
            }
            for (func.params, 0..) |p, i| {
                if (i > 0) try self.write(", ");
                try self.write(sanitizeName(p.name));
            }
            try self.write(");\n");

            // Store in cache
            try self.writeIndent();
            if (std.mem.eql(u8, ret_type, "void")) {
                try self.writeFmt("_kira_memo_store(&_cache, _args, {d}, 0);\n", .{func.params.len});
            } else if (std.mem.eql(u8, ret_type, "const char*")) {
                try self.writeFmt("_kira_memo_store(&_cache, _args, {d}, (kira_int)(intptr_t)_result);\n", .{func.params.len});
            } else {
                try self.writeFmt("_kira_memo_store(&_cache, _args, {d}, (kira_int)_result);\n", .{func.params.len});
            }
        }

        // Return result
        try self.writeIndent();
        if (std.mem.eql(u8, ret_type, "void")) {
            // void functions don't return a value (already called impl above)
        } else {
            try self.write("return _result;\n");
        }

        self.indent_level -= 1;
        try self.write("}\n\n");
    }

    /// Check if a Kira type name is an integer-like type suitable for array-indexed caching.
    fn isIntLikeType(type_name: []const u8) bool {
        const int_types = [_][]const u8{ "i8", "i16", "i32", "i64", "u8", "u16", "u32", "u64", "char" };
        for (int_types) |t| {
            if (std.mem.eql(u8, type_name, t)) return true;
        }
        return false;
    }

    fn emitFunctionName(self: *CCodeGen, func: *const Function) CodeGenError!void {
        if (func.name) |name| {
            try self.write("kira_");
            try self.write(name);
        } else {
            try self.write("kira_anon");
        }
    }

    fn emitBlock(self: *CCodeGen, func: *const Function, blk: *const ir.BasicBlock) CodeGenError!void {
        // Block label
        try self.writeIndent();
        try self.writeFmt("bb{d}:;\n", .{blk.id});

        // Instructions
        for (blk.instructions.items) |ref| {
            const inst = func.getInstruction(ref);
            try self.emitInstruction(func, ref, inst);
        }

        // Terminator
        try self.emitTerminator(func, blk, &blk.terminator);
    }

    /// Check if a block contains any phi instructions.
    fn blockHasPhi(_: *CCodeGen, func: *const Function, block_id: BlockId) bool {
        if (block_id >= func.blocks.items.len) return false;
        const blk = &func.blocks.items[block_id];
        for (blk.instructions.items) |inst_ref| {
            const inst = func.getInstruction(inst_ref);
            if (inst.op == .phi) return true;
        }
        return false;
    }

    /// Emit phi assignments for a specific predecessor->successor edge.
    fn emitPhiAssignmentsForEdge(self: *CCodeGen, func: *const Function, from_block: BlockId, to_block: BlockId) CodeGenError!void {
        if (to_block >= func.blocks.items.len) return;
        const succ = &func.blocks.items[to_block];
        for (succ.instructions.items) |inst_ref| {
            const inst = func.getInstruction(inst_ref);
            switch (inst.op) {
                .phi => |p| {
                    for (p.incoming) |inc| {
                        if (inc.block == from_block) {
                            try self.writeIndent();
                            try self.writeFmt("v{d} = v{d};\n", .{ inst_ref, inc.value });
                        }
                    }
                },
                else => {},
            }
        }
    }

    // ----------------------------------------------------------------
    // Instruction emission
    // ----------------------------------------------------------------

    fn emitInstruction(self: *CCodeGen, func: *const Function, ref: ValueRef, inst: *const Instruction) CodeGenError!void {
        try self.writeIndent();
        switch (inst.op) {
            .const_int => |c| try self.writeFmt("v{d} = {d};\n", .{ ref, c.value }),
            .const_float => |f| try self.writeFmt("v{d} = *(kira_int*)&(kira_float){{{d}}};\n", .{ ref, f }),
            .const_bool => |b| try self.writeFmt("v{d} = {d};\n", .{ ref, @as(u1, if (b) 1 else 0) }),
            .const_char => |c| try self.writeFmt("v{d} = {d};\n", .{ ref, c }),
            .const_void => try self.writeFmt("v{d} = 0;\n", .{ref}),
            .const_string => |s| try self.writeFmt("v{d} = (kira_int)(intptr_t)\"{s}\";\n", .{ ref, s }),
            .int_binop => |b| {
                const op_str: []const u8 = switch (b.op) {
                    .add => "+",
                    .sub => "-",
                    .mul => "*",
                    .div => "/",
                    .mod => "%",
                };
                try self.writeFmt("v{d} = v{d} {s} v{d};\n", .{ ref, b.left, op_str, b.right });
            },
            .float_binop => |b| {
                // Unpack i64 bit-patterns to double, operate, repack
                if (b.op == .mod) {
                    try self.writeFmt("{{ kira_float _a, _b, _r; memcpy(&_a, &v{d}, sizeof(kira_float)); memcpy(&_b, &v{d}, sizeof(kira_float)); _r = fmod(_a, _b); memcpy(&v{d}, &_r, sizeof(kira_float)); }}\n", .{ b.left, b.right, ref });
                } else {
                    const op_str: []const u8 = switch (b.op) {
                        .add => "+",
                        .sub => "-",
                        .mul => "*",
                        .div => "/",
                        .mod => unreachable,
                    };
                    try self.writeFmt("{{ kira_float _a, _b, _r; memcpy(&_a, &v{d}, sizeof(kira_float)); memcpy(&_b, &v{d}, sizeof(kira_float)); _r = _a {s} _b; memcpy(&v{d}, &_r, sizeof(kira_float)); }}\n", .{ b.left, b.right, op_str, ref });
                }
            },
            .int_neg => |v| try self.writeFmt("v{d} = -v{d};\n", .{ ref, v }),
            .float_neg => |v| try self.writeFmt("{{ kira_float _f; memcpy(&_f, &v{d}, sizeof(kira_float)); _f = -_f; memcpy(&v{d}, &_f, sizeof(kira_float)); }}\n", .{ v, ref }),
            .cmp => |c| {
                // String comparison: use strcmp instead of pointer equality
                if (self.string_values.contains(c.left) or self.string_values.contains(c.right)) {
                    const cmp_op: []const u8 = switch (c.op) {
                        .eq => "== 0",
                        .ne => "!= 0",
                        .lt => "< 0",
                        .le => "<= 0",
                        .gt => "> 0",
                        .ge => ">= 0",
                    };
                    try self.writeFmt("v{d} = (strcmp((const char*)(intptr_t)v{d}, (const char*)(intptr_t)v{d}) {s});\n", .{ ref, c.left, c.right, cmp_op });
                } else {
                    const op_str: []const u8 = switch (c.op) {
                        .eq => "==",
                        .ne => "!=",
                        .lt => "<",
                        .le => "<=",
                        .gt => ">",
                        .ge => ">=",
                    };
                    try self.writeFmt("v{d} = (v{d} {s} v{d});\n", .{ ref, c.left, op_str, c.right });
                }
            },
            .log_not => |v| try self.writeFmt("v{d} = !v{d};\n", .{ ref, v }),
            .int_to_float => |v| try self.writeFmt("{{ kira_float _f = (kira_float)v{d}; memcpy(&v{d}, &_f, sizeof(kira_float)); }}\n", .{ v, ref }),
            .float_to_int => |v| try self.writeFmt("{{ kira_float _f; memcpy(&_f, &v{d}, sizeof(kira_float)); v{d} = (kira_int)_f; }}\n", .{ v, ref }),
            .to_string => |v| {
                // Convert integer to string via snprintf
                try self.writeFmt("{{ char* _s = (char*)KIRA_ALLOC(32); snprintf(_s, 32, \"%lld\", (long long)v{d}); v{d} = (kira_int)(intptr_t)_s; }}\n", .{ v, ref });
            },
            .alloc_var => |a| {
                if (a.init_value) |iv| {
                    try self.writeFmt("v{d} = v{d}; /* var {s} */\n", .{ ref, iv, a.name });
                } else {
                    try self.writeFmt("v{d} = 0; /* var {s} */\n", .{ ref, a.name });
                }
            },
            .load_var => |v| try self.writeFmt("v{d} = v{d};\n", .{ ref, v }),
            .store_var => |s| try self.writeFmt("v{d} = v{d}; v{d} = 0;\n", .{ s.target, s.value, ref }),
            .func_ref => |name| {
                // Emit function pointer as kira_int
                try self.writeFmt("v{d} = (kira_int)(intptr_t)&kira_{s};\n", .{ ref, name });
            },
            .call_direct => |c| {
                // Direct call to a named function
                // Check if function returns void to avoid assigning void
                const is_void = if (self.current_module) |m| blk: {
                    if (m.function_map.get(c.name)) |idx| {
                        break :blk std.mem.eql(u8, m.functions.items[idx].return_type_name, "void");
                    }
                    break :blk false;
                } else false;

                // Check if function returns a pointer type (string) that needs casting
                const needs_cast = if (self.current_module) |m| blk: {
                    if (m.function_map.get(c.name)) |idx| {
                        const rtn = m.functions.items[idx].return_type_name;
                        break :blk std.mem.eql(u8, rtn, "string");
                    }
                    break :blk false;
                } else false;

                if (is_void) {
                    try self.writeFmt("kira_{s}(", .{c.name});
                } else if (needs_cast) {
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)kira_{s}(", .{ ref, c.name });
                } else {
                    try self.writeFmt("v{d} = kira_{s}(", .{ ref, c.name });
                }
                // Look up target function params for type-aware argument casting
                const target_params: ?[]const Function.Param = if (self.current_module) |m| blk: {
                    if (m.function_map.get(c.name)) |idx| {
                        break :blk m.functions.items[idx].params;
                    }
                    break :blk null;
                } else null;

                for (c.args, 0..) |a, i| {
                    if (i > 0) try self.write(", ");
                    // Cast kira_int to proper C type for string/float params
                    if (target_params) |params| {
                        if (i < params.len and std.mem.eql(u8, params[i].type_name, "string")) {
                            try self.writeFmt("(const char*)(intptr_t)v{d}", .{a});
                        } else if (i < params.len and (std.mem.eql(u8, params[i].type_name, "f32") or std.mem.eql(u8, params[i].type_name, "f64"))) {
                            try self.writeFmt("({s})v{d}", .{ kiraTypeToCType(params[i].type_name), a });
                        } else {
                            try self.writeFmt("v{d}", .{a});
                        }
                    } else {
                        try self.writeFmt("v{d}", .{a});
                    }
                }
                if (is_void) {
                    try self.writeFmt("); v{d} = 0;\n", .{ref});
                } else {
                    try self.write(");\n");
                }
            },
            .call_builtin => |c| {
                // Runtime builtin calls
                if (std.mem.eql(u8, c.name, "println")) {
                    if (c.args.len > 0) {
                        try self.writeFmt("printf(\"%s\\n\", (const char*)(intptr_t)v{d}); v{d} = 0;\n", .{ c.args[0], ref });
                    } else {
                        try self.writeFmt("printf(\"\\n\"); v{d} = 0;\n", .{ref});
                    }
                } else if (std.mem.eql(u8, c.name, "print")) {
                    if (c.args.len > 0) {
                        try self.writeFmt("printf(\"%s\", (const char*)(intptr_t)v{d}); v{d} = 0;\n", .{ c.args[0], ref });
                    } else {
                        try self.writeFmt("v{d} = 0;\n", .{ref});
                    }
                } else if (std.mem.eql(u8, c.name, "eprintln")) {
                    if (c.args.len > 0) {
                        try self.writeFmt("fprintf(stderr, \"%s\\n\", (const char*)(intptr_t)v{d}); v{d} = 0;\n", .{ c.args[0], ref });
                    } else {
                        try self.writeFmt("fprintf(stderr, \"\\n\"); v{d} = 0;\n", .{ref});
                    }
                } else if (std.mem.eql(u8, c.name, "int_to_string")) {
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)kira_int_to_string(v{d});\n", .{ ref, c.args[0] });
                } else if (std.mem.eql(u8, c.name, "float_to_string")) {
                    try self.writeFmt("{{ char* _s = (char*)KIRA_ALLOC(32); kira_float _f; memcpy(&_f, &v{d}, sizeof(kira_float)); snprintf(_s, 32, \"%g\", _f); v{d} = (kira_int)(intptr_t)_s; }}\n", .{ c.args[0], ref });
                } else if (std.mem.eql(u8, c.name, "string_length")) {
                    try self.writeFmt("v{d} = (kira_int)strlen((const char*)(intptr_t)v{d});\n", .{ ref, c.args[0] });

                    // --- Tier 1: already lowered, now codegen'd ---
                } else if (std.mem.eql(u8, c.name, "string_char_at")) {
                    try self.writeFmt("v{d} = (kira_int)((const char*)(intptr_t)v{d})[v{d}];\n", .{ ref, c.args[0], c.args[1] });
                } else if (std.mem.eql(u8, c.name, "string_trim")) {
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)kira_string_trim((const char*)(intptr_t)v{d});\n", .{ ref, c.args[0] });
                } else if (std.mem.eql(u8, c.name, "string_split")) {
                    try self.writeFmt("v{d} = kira_string_split((const char*)(intptr_t)v{d}, (const char*)(intptr_t)v{d});\n", .{ ref, c.args[0], c.args[1] });
                } else if (std.mem.eql(u8, c.name, "char_to_int")) {
                    try self.writeFmt("v{d} = v{d};\n", .{ ref, c.args[0] });

                    // --- Tier 2: string operations ---
                } else if (std.mem.eql(u8, c.name, "string_contains")) {
                    try self.writeFmt("v{d} = (strstr((const char*)(intptr_t)v{d}, (const char*)(intptr_t)v{d}) != NULL) ? 1 : 0;\n", .{ ref, c.args[0], c.args[1] });
                } else if (std.mem.eql(u8, c.name, "string_starts_with")) {
                    try self.writeFmt("v{d} = (strncmp((const char*)(intptr_t)v{d}, (const char*)(intptr_t)v{d}, strlen((const char*)(intptr_t)v{d})) == 0) ? 1 : 0;\n", .{ ref, c.args[0], c.args[1], c.args[1] });
                } else if (std.mem.eql(u8, c.name, "string_ends_with")) {
