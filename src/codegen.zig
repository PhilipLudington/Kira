    fn emitTypeDecls(self: *CCodeGen, module: *const Module) CodeGenError!void {
        if (module.type_decls.items.len == 0) return;
        try self.write("/* Type declarations */\n");
        for (module.type_decls.items) |td| {
            switch (td.kind) {
                .sum_type => |st| {
                    try self.write("/* Sum type: ");
                    try self.write(td.name);
                    try self.write(" */\n");
                    try self.write("typedef struct {\n");
                    self.indent_level += 1;
                    try self.writeIndent();
                    try self.write("uint32_t tag;\n");
                    // Find max field count across variants
                    var max_fields: u32 = 0;
                    for (st.variants) |v| {
                        if (v.field_count > max_fields) max_fields = v.field_count;
                    }
                    if (max_fields > 0) {
                        try self.writeIndent();
                        try self.writeFmt("kira_int fields[{d}];\n", .{max_fields});
                    }
                    self.indent_level -= 1;
                    try self.write("} kira_");
                    try self.write(td.name);
                    try self.write(";\n\n");

                    // Emit tag constants
                    for (st.variants) |v| {
                        try self.writeFmt("#define TAG_{s}_{s} {d}\n", .{ td.name, v.name, v.tag });
                    }
                    try self.write("\n");
                },
                .product_type => |pt| {
                    try self.write("/* Record type: ");
                    try self.write(td.name);
                    try self.write(" */\n");
                    try self.write("typedef struct {\n");
                    self.indent_level += 1;
                    for (pt.fields) |f| {
                        try self.writeIndent();
                        try self.write(kiraTypeToCType(f.type_name));
                        try self.writeFmt(" {s};\n", .{f.name});
                    }
                    self.indent_level -= 1;
                    try self.write("} kira_");
                    try self.write(td.name);
                    try self.write(";\n\n");
                },
            }
        }
    }

    // ----------------------------------------------------------------
    // Forward declarations
    // ----------------------------------------------------------------

    fn emitFunctionDeclarations(self: *CCodeGen, module: *const Module) CodeGenError!void {
        if (module.functions.items.len == 0) return;
        try self.write("/* Forward declarations */\n");
        for (module.functions.items) |*func| {
            if (func.is_effect) {
                try self.write("/* effect */ ");
            } else {
                try self.write("/* pure */ ");
            }
            if (func.is_memoized) {
                try self.write("/* memo */ ");
            }
            try self.write(kiraTypeToCType(func.return_type_name));
            try self.write(" ");
            try self.emitFunctionName(func);
            try self.write("(kira_int _env");
            for (func.params) |p| {
                try self.write(", ");
                try self.write(kiraTypeToCType(p.type_name));
                try self.writeFmt(" {s}", .{sanitizeName(p.name)});
            }
            try self.write(");\n");

            // For memoized functions, also forward-declare the _memo_impl version
            if (func.is_memoized) {
                try self.write("static ");
                try self.write(kiraTypeToCType(func.return_type_name));
                try self.write(" ");
                try self.emitFunctionName(func);
                try self.write("_memo_impl(kira_int _env");
                for (func.params) |p| {
                    try self.write(", ");
                    try self.write(kiraTypeToCType(p.type_name));
                    try self.writeFmt(" {s}", .{sanitizeName(p.name)});
                }
                try self.write(");\n");
            }
        }
        try self.write("\n");
    }

    // ----------------------------------------------------------------
    // Constants
    // ----------------------------------------------------------------

    fn emitConstants(self: *CCodeGen, module: *const Module) CodeGenError!void {
        if (module.constants.items.len == 0) return;
        try self.write("/* Constants */\n");
        for (module.constants.items) |c| {
            switch (c.value) {
                .integer => |v| try self.writeFmt("static const int64_t kira_const_{s} = {d};\n", .{ c.name, v }),
                .float => |v| try self.writeFmt("static const double kira_const_{s} = {d};\n", .{ c.name, v }),
                .boolean => |b| try self.writeFmt("static const bool kira_const_{s} = {s};\n", .{ c.name, if (b) "true" else "false" }),
                .string => |s| {
                    try self.writeFmt("static const char* kira_const_{s} = \"", .{c.name});
                    try self.emitEscapedString(s);
                    try self.write("\";\n");
                },
                .char => |c_val| try self.writeFmt("static const uint32_t kira_const_{s} = {d};\n", .{ c.name, c_val }),
                .void_val => try self.writeFmt("static const int64_t kira_const_{s} = 0;\n", .{c.name}),
            }
        }
        try self.write("\n");
    }

    // ----------------------------------------------------------------
    // Function emission
    // ----------------------------------------------------------------

    fn emitFunction(self: *CCodeGen, func: *const Function) CodeGenError!void {
        // Clear per-function type tracking
        self.string_values.clearRetainingCapacity();
        self.bool_values.clearRetainingCapacity();
        // Pre-scan: mark typed params
        for (func.params) |p| {
            if (std.mem.eql(u8, p.type_name, "string")) {
                self.string_values.put(self.allocator, p.value_ref, {}) catch {};
            } else if (std.mem.eql(u8, p.type_name, "bool")) {
                self.bool_values.put(self.allocator, p.value_ref, {}) catch {};
            }
        }
        // Pre-scan: mark string-producing and bool-producing instructions
        for (func.instructions.items, 0..) |inst, idx| {
            const ref: ValueRef = @intCast(idx);
            switch (inst.op) {
                .const_string => self.string_values.put(self.allocator, ref, {}) catch {},
                .str_concat => self.string_values.put(self.allocator, ref, {}) catch {},
                .to_string => self.string_values.put(self.allocator, ref, {}) catch {},
                .const_bool => self.bool_values.put(self.allocator, ref, {}) catch {},
                .call_builtin => |cb| {
                    if (builtin_registry.get(cb.name)) |spec| {
                        if (spec.returns_string) {
                            self.string_values.put(self.allocator, ref, {}) catch {};
                        }
                    }
                },
                else => {},
            }
        }

        // Function signature with effect annotation
        if (func.is_effect) {
            try self.write("/* effect */ ");
        } else {
            try self.write("/* pure */ ");
        }
        if (func.is_memoized) {
            try self.write("/* memo impl */ ");
        }
        try self.write(kiraTypeToCType(func.return_type_name));
        try self.write(" ");
        try self.emitFunctionName(func);
        if (func.is_memoized) try self.write("_memo_impl");
        try self.write("(kira_int _env");
        for (func.params) |p| {
            try self.write(", ");
            try self.write(kiraTypeToCType(p.type_name));
            try self.writeFmt(" {s}", .{sanitizeName(p.name)});
        }
        try self.write(") {\n");
        self.indent_level += 1;

        // Effect tracking: pure functions enter pure context
        if (!func.is_effect) {
            try self.writeIndent();
            try self.write("KIRA_ENTER_PURE();\n");
        }

        // Declare all SSA variables
        if (func.instructions.items.len > 0) {
            try self.writeIndent();
            try self.write("kira_int ");
            for (0..func.instructions.items.len) |i| {
                if (i > 0) try self.write(", ");
                try self.writeFmt("v{d}", .{i});
            }
            try self.write(";\n");
        }

        // Emit basic blocks
        for (func.blocks.items) |*blk| {
            try self.emitBlock(func, blk);
        }

        self.indent_level -= 1;
        try self.write("}\n\n");

        // For memoized functions, emit the caching wrapper
        if (func.is_memoized) {
            try self.emitMemoWrapper(func);
        }
    }

    /// Emit a memoization caching wrapper for a memoized function.
    /// The wrapper checks a cache before delegating to the _memo_impl function.
    fn emitMemoWrapper(self: *CCodeGen, func: *const Function) CodeGenError!void {
        const ret_type = kiraTypeToCType(func.return_type_name);

        // Determine if we can use the fast array cache (single int-like param)
        const use_array_cache = func.params.len == 1 and isIntLikeType(func.params[0].type_name);

        try self.write("/* memo wrapper */ ");
        try self.write(ret_type);
        try self.write(" ");
        try self.emitFunctionName(func);
        try self.write("(kira_int _env");
        for (func.params) |p| {
            try self.write(", ");
            try self.write(kiraTypeToCType(p.type_name));
            try self.writeFmt(" {s}", .{sanitizeName(p.name)});
        }
        try self.write(") {\n");
        self.indent_level += 1;

        if (use_array_cache) {
            // Fast path: single int param → static array cache
            try self.writeIndent();
            try self.writeFmt("static {s} _cache[KIRA_MEMO_ARRAY_SIZE];\n", .{ret_type});
            try self.writeIndent();
            try self.write("static bool _set[KIRA_MEMO_ARRAY_SIZE] = {0};\n");

            const param_name = sanitizeName(func.params[0].name);
            // Cache lookup
            try self.writeIndent();
            try self.writeFmt("if ({s} >= 0 && {s} < KIRA_MEMO_ARRAY_SIZE && _set[{s}]) return _cache[{s}];\n", .{ param_name, param_name, param_name, param_name });

            // Call impl
            try self.writeIndent();
            try self.writeFmt("{s} _result = ", .{ret_type});
            try self.emitFunctionName(func);
            try self.writeFmt("_memo_impl(_env, {s});\n", .{param_name});

            // Store in cache
            try self.writeIndent();
            try self.writeFmt("if ({s} >= 0 && {s} < KIRA_MEMO_ARRAY_SIZE) {{ _cache[{s}] = _result; _set[{s}] = true; }}\n", .{ param_name, param_name, param_name, param_name });
        } else {
            // General path: linked-list cache
            try self.writeIndent();
            try self.write("static _KiraMemoCache _cache = {0};\n");
            try self.writeIndent();
            try self.writeFmt("kira_int _args[{d}]", .{@max(func.params.len, 1)});
            if (func.params.len == 0) {
                try self.write(" = {0};\n");
            } else {
                try self.write(";\n");
                for (func.params, 0..) |p, i| {
                    try self.writeIndent();
                    try self.writeFmt("_args[{d}] = (kira_int){s};\n", .{ i, sanitizeName(p.name) });
                }
            }

            // Cache lookup
            try self.writeIndent();
            try self.write("bool _found = false;\n");
            try self.writeIndent();
            try self.writeFmt("kira_int _cached = _kira_memo_lookup(&_cache, _args, {d}, &_found);\n", .{func.params.len});
            try self.writeIndent();
            if (std.mem.eql(u8, ret_type, "const char*")) {
                try self.write("if (_found) return (const char*)(intptr_t)_cached;\n");
            } else if (std.mem.eql(u8, ret_type, "void")) {
                try self.write("if (_found) return;\n");
            } else {
                try self.writeFmt("if (_found) return ({s})_cached;\n", .{ret_type});
            }

            // Call impl
            try self.writeIndent();
            if (std.mem.eql(u8, ret_type, "void")) {
                try self.emitFunctionName(func);
                try self.write("_memo_impl(_env");
            } else {
                try self.writeFmt("{s} _result = ", .{ret_type});
                try self.emitFunctionName(func);
                try self.write("_memo_impl(_env");
            }
            for (func.params) |p| {
                try self.write(", ");
                try self.write(sanitizeName(p.name));
            }
            try self.write(");\n");

            // Store in cache
            try self.writeIndent();
            if (std.mem.eql(u8, ret_type, "void")) {
                try self.writeFmt("_kira_memo_store(&_cache, _args, {d}, 0);\n", .{func.params.len});
            } else if (std.mem.eql(u8, ret_type, "const char*")) {
                try self.writeFmt("_kira_memo_store(&_cache, _args, {d}, (kira_int)(intptr_t)_result);\n", .{func.params.len});
            } else {
                try self.writeFmt("_kira_memo_store(&_cache, _args, {d}, (kira_int)_result);\n", .{func.params.len});
            }
        }

        // Return result
        try self.writeIndent();
        if (std.mem.eql(u8, ret_type, "void")) {
            // void functions don't return a value (already called impl above)
        } else {
            try self.write("return _result;\n");
        }

        self.indent_level -= 1;
        try self.write("}\n\n");
    }

    /// Check if a Kira type name is an integer-like type suitable for array-indexed caching.
    fn isIntLikeType(type_name: []const u8) bool {
        const int_types = [_][]const u8{ "i8", "i16", "i32", "i64", "u8", "u16", "u32", "u64", "char" };
        for (int_types) |t| {
            if (std.mem.eql(u8, type_name, t)) return true;
        }
        return false;
    }

    fn emitFunctionName(self: *CCodeGen, func: *const Function) CodeGenError!void {
        if (func.name) |name| {
            try self.write("kira_");
            try self.write(name);
        } else {
            // Unique name for anonymous closures using function index
            try self.write("kira_anon_");
            if (self.current_module) |m| {
                for (m.functions.items, 0..) |*f, i| {
                    if (f == func) {
                        try self.writeFmt("{d}", .{i});
                        return;
                    }
                }
            }
            try self.write("0");
        }
    }

    fn emitBlock(self: *CCodeGen, func: *const Function, blk: *const ir.BasicBlock) CodeGenError!void {
        // Block label
        try self.writeIndent();
        try self.writeFmt("bb{d}:;\n", .{blk.id});

        // Instructions
        for (blk.instructions.items) |ref| {
            const inst = func.getInstruction(ref);
            try self.emitInstruction(func, ref, inst);
        }

        // Terminator
        try self.emitTerminator(func, blk, &blk.terminator);
    }

    /// Check if a block contains any phi instructions.
    fn blockHasPhi(_: *CCodeGen, func: *const Function, block_id: BlockId) bool {
        if (block_id >= func.blocks.items.len) return false;
        const blk = &func.blocks.items[block_id];
        for (blk.instructions.items) |inst_ref| {
            const inst = func.getInstruction(inst_ref);
            if (inst.op == .phi) return true;
        }
        return false;
    }

    /// Emit phi assignments for a specific predecessor->successor edge.
    fn emitPhiAssignmentsForEdge(self: *CCodeGen, func: *const Function, from_block: BlockId, to_block: BlockId) CodeGenError!void {
        if (to_block >= func.blocks.items.len) return;
        const succ = &func.blocks.items[to_block];
        for (succ.instructions.items) |inst_ref| {
            const inst = func.getInstruction(inst_ref);
            switch (inst.op) {
                .phi => |p| {
                    for (p.incoming) |inc| {
                        if (inc.block == from_block) {
                            try self.writeIndent();
                            try self.writeFmt("v{d} = v{d};\n", .{ inst_ref, inc.value });
                        }
                    }
                },
                else => {},
            }
        }
    }

    // ----------------------------------------------------------------
    // Instruction emission
    // ----------------------------------------------------------------

    fn emitInstruction(self: *CCodeGen, func: *const Function, ref: ValueRef, inst: *const Instruction) CodeGenError!void {
        try self.writeIndent();
        switch (inst.op) {
            .const_int => |c| try self.writeFmt("v{d} = {d};\n", .{ ref, c.value }),
            .const_float => |f| try self.writeFmt("v{d} = *(kira_int*)&(kira_float){{{d}}};\n", .{ ref, f }),
            .const_bool => |b| try self.writeFmt("v{d} = {d};\n", .{ ref, @as(u1, if (b) 1 else 0) }),
            .const_char => |c| try self.writeFmt("v{d} = {d};\n", .{ ref, c }),
            .const_void => try self.writeFmt("v{d} = 0;\n", .{ref}),
            .const_string => |s| {
                try self.writeFmt("v{d} = (kira_int)(intptr_t)\"", .{ref});
                try self.emitEscapedString(s);
                try self.write("\";\n");
            },
            .int_binop => |b| {
                const op_str: []const u8 = switch (b.op) {
                    .add => "+",
                    .sub => "-",
                    .mul => "*",
                    .div => "/",
                    .mod => "%",
                };
                try self.writeFmt("v{d} = v{d} {s} v{d};\n", .{ ref, b.left, op_str, b.right });
            },
            .float_binop => |b| {
                // Unpack i64 bit-patterns to double, operate, repack
                if (b.op == .mod) {
                    try self.writeFmt("{{ kira_float _a, _b, _r; memcpy(&_a, &v{d}, sizeof(kira_float)); memcpy(&_b, &v{d}, sizeof(kira_float)); _r = fmod(_a, _b); memcpy(&v{d}, &_r, sizeof(kira_float)); }}\n", .{ b.left, b.right, ref });
                } else {
                    const op_str: []const u8 = switch (b.op) {
                        .add => "+",
                        .sub => "-",
                        .mul => "*",
                        .div => "/",
                        .mod => unreachable,
                    };
                    try self.writeFmt("{{ kira_float _a, _b, _r; memcpy(&_a, &v{d}, sizeof(kira_float)); memcpy(&_b, &v{d}, sizeof(kira_float)); _r = _a {s} _b; memcpy(&v{d}, &_r, sizeof(kira_float)); }}\n", .{ b.left, b.right, op_str, ref });
                }
            },
            .int_neg => |v| try self.writeFmt("v{d} = -v{d};\n", .{ ref, v }),
            .float_neg => |v| try self.writeFmt("{{ kira_float _f; memcpy(&_f, &v{d}, sizeof(kira_float)); _f = -_f; memcpy(&v{d}, &_f, sizeof(kira_float)); }}\n", .{ v, ref }),
            .cmp => |c| {
                // String comparison: use strcmp instead of pointer equality
                if (self.string_values.contains(c.left) or self.string_values.contains(c.right)) {
                    const cmp_op: []const u8 = switch (c.op) {
                        .eq => "== 0",
                        .ne => "!= 0",
                        .lt => "< 0",
                        .le => "<= 0",
                        .gt => "> 0",
                        .ge => ">= 0",
                    };
                    try self.writeFmt("v{d} = (strcmp((const char*)(intptr_t)v{d}, (const char*)(intptr_t)v{d}) {s});\n", .{ ref, c.left, c.right, cmp_op });
                } else {
                    const op_str: []const u8 = switch (c.op) {
                        .eq => "==",
                        .ne => "!=",
                        .lt => "<",
                        .le => "<=",
                        .gt => ">",
                        .ge => ">=",
                    };
                    try self.writeFmt("v{d} = (v{d} {s} v{d});\n", .{ ref, c.left, op_str, c.right });
                }
            },
            .log_not => |v| try self.writeFmt("v{d} = !v{d};\n", .{ ref, v }),
            .int_to_float => |v| try self.writeFmt("{{ kira_float _f = (kira_float)v{d}; memcpy(&v{d}, &_f, sizeof(kira_float)); }}\n", .{ v, ref }),
            .float_to_int => |v| try self.writeFmt("{{ kira_float _f; memcpy(&_f, &v{d}, sizeof(kira_float)); v{d} = (kira_int)_f; }}\n", .{ v, ref }),
            .to_string => |v| {
                if (self.string_values.contains(v)) {
                    // Already a string — pass through
                    try self.writeFmt("v{d} = v{d};\n", .{ ref, v });
                } else if (self.bool_values.contains(v)) {
                    // Bool to string: "true" or "false"
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)(v{d} ? \"true\" : \"false\");\n", .{ ref, v });
                } else {
                    // Convert integer to string via snprintf
                    try self.writeFmt("{{ char* _s = (char*)KIRA_ALLOC(32); snprintf(_s, 32, \"%lld\", (long long)v{d}); v{d} = (kira_int)(intptr_t)_s; }}\n", .{ v, ref });
                }
            },
            .alloc_var => |a| {
                if (a.init_value) |iv| {
                    try self.writeFmt("v{d} = v{d}; /* var {s} */\n", .{ ref, iv, a.name });
                } else {
                    try self.writeFmt("v{d} = 0; /* var {s} */\n", .{ ref, a.name });
                }
            },
            .load_var => |v| try self.writeFmt("v{d} = v{d};\n", .{ ref, v }),
            .store_var => |s| try self.writeFmt("v{d} = v{d}; v{d} = 0;\n", .{ s.target, s.value, ref }),
            .func_ref => |name| {
                // Wrap function pointer in closure struct [func_ptr] for uniform calling convention
                try self.writeFmt("{{ kira_int* _fr = (kira_int*)KIRA_ALLOC(sizeof(kira_int)); _fr[0] = (kira_int)(intptr_t)&kira_{s}; v{d} = (kira_int)(intptr_t)_fr; }}\n", .{ name, ref });
            },
            .call_direct => |c| {
                // Direct call to a named function
                // Check if function returns void to avoid assigning void
                const is_void = if (self.current_module) |m| blk: {
                    if (m.function_map.get(c.name)) |idx| {
                        break :blk std.mem.eql(u8, m.functions.items[idx].return_type_name, "void");
                    }
                    break :blk false;
                } else false;

                // Check if function returns a pointer type (string) that needs casting
                const needs_cast = if (self.current_module) |m| blk: {
                    if (m.function_map.get(c.name)) |idx| {
                        const rtn = m.functions.items[idx].return_type_name;
                        break :blk std.mem.eql(u8, rtn, "string");
                    }
                    break :blk false;
                } else false;

                if (is_void) {
                    try self.writeFmt("kira_{s}(0", .{c.name});
                } else if (needs_cast) {
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)kira_{s}(0", .{ ref, c.name });
                } else {
                    try self.writeFmt("v{d} = kira_{s}(0", .{ ref, c.name });
                }
                // Look up target function params for type-aware argument casting
                const target_params: ?[]const Function.Param = if (self.current_module) |m| blk: {
                    if (m.function_map.get(c.name)) |idx| {
                        break :blk m.functions.items[idx].params;
                    }
                    break :blk null;
                } else null;

                for (c.args, 0..) |a, i| {
                    try self.write(", ");
                    // Cast kira_int to proper C type for string/float params
                    if (target_params) |params| {
                        if (i < params.len and std.mem.eql(u8, params[i].type_name, "string")) {
                            try self.writeFmt("(const char*)(intptr_t)v{d}", .{a});
                        } else if (i < params.len and (std.mem.eql(u8, params[i].type_name, "f32") or std.mem.eql(u8, params[i].type_name, "f64"))) {
                            try self.writeFmt("({s})v{d}", .{ kiraTypeToCType(params[i].type_name), a });
                        } else {
                            try self.writeFmt("v{d}", .{a});
                        }
                    } else {
                        try self.writeFmt("v{d}", .{a});
                    }
                }
                if (is_void) {
                    try self.writeFmt("); v{d} = 0;\n", .{ref});
                } else {
                    try self.write(");\n");
                }
            },
            .call_builtin => |c| {
                // Dispatch via builtin registry (table-driven)
                if (builtin_registry.get(c.name)) |spec| {
                    try spec.emit(self, ref, c.args);
                } else {
                    // Unknown builtin — emit as comment for debugging
                    try self.writeFmt("v{d} = 0; /* unknown builtin: {s} */\n", .{ ref, c.name });
                }
            },
            .call => |c| {
                // Indirect call: extract func_ptr from closure[0], pass closure as _env
                try self.writeFmt("v{d} = ((kira_int(*)(kira_int", .{ref});
                for (0..c.args.len) |_| {
                    try self.write(", kira_int");
                }
                try self.writeFmt("))(((kira_int*)(intptr_t)v{d})[0]))(v{d}", .{ c.callee, c.callee });
                for (c.args) |a| {
                    try self.writeFmt(", v{d}", .{a});
                }
                try self.write(");\n");
            },
            .make_closure => |c| {
                // Layout: [function_ptr][capture0][capture1]...
                try self.writeFmt("{{ kira_int* _c = (kira_int*)KIRA_ALLOC({d} * sizeof(kira_int)); _c[0] = (kira_int)(intptr_t)&", .{c.captures.len + 1});
                // Emit the function name reference
                const module = self.current_module;
                if (module) |m| {
                    if (c.function_index < m.functions.items.len) {
                        const target_func = &m.functions.items[c.function_index];
                        try self.emitFunctionName(target_func);
                    } else {
                        try self.write("kira_anon");
                    }
                } else {
                    try self.write("kira_anon");
                }
                try self.write("; ");
                for (c.captures, 0..) |cap, i| {
                    try self.writeFmt("_c[{d}] = v{d}; ", .{ i + 1, cap });
                }
                try self.writeFmt("v{d} = (kira_int)(intptr_t)_c; }}\n", .{ref});
            },
            .make_tuple => |elems| {
                // Heap-allocate a kira_int array, store elements, cast to kira_int
                try self.writeFmt("{{ kira_int* _t = (kira_int*)KIRA_ALLOC({d} * sizeof(kira_int)); ", .{elems.len});
                for (elems, 0..) |elem, i| {
                    try self.writeFmt("_t[{d}] = v{d}; ", .{ i, elem });
                }
                try self.writeFmt("v{d} = (kira_int)(intptr_t)_t; }}\n", .{ref});
            },
            .make_array => |elems| {
                // Layout: [length][elem0][elem1]... — length at index 0, elements at 1..N
                try self.writeFmt("{{ kira_int* _a = (kira_int*)KIRA_ALLOC({d} * sizeof(kira_int)); _a[0] = {d}; ", .{ elems.len + 1, elems.len });
                for (elems, 0..) |elem, i| {
                    try self.writeFmt("_a[{d}] = v{d}; ", .{ i + 1, elem });
                }
                try self.writeFmt("v{d} = (kira_int)(intptr_t)_a; }}\n", .{ref});
            },
            .make_record => |r| {
                // Heap-allocate a kira_int array for fields, cast to kira_int
                try self.writeFmt("{{ kira_int* _r = (kira_int*)KIRA_ALLOC({d} * sizeof(kira_int)); ", .{r.field_values.len});
                for (r.field_values, 0..) |fv, i| {
                    try self.writeFmt("_r[{d}] = v{d}; ", .{ i, fv });
                }
                if (r.type_name) |tn| {
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)_r; }} /* record {s} */\n", .{ ref, tn });
                } else {
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)_r; }}\n", .{ref});
                }
            },
            .make_variant => |v| {
                const tag = self.lookupVariantTag(v.type_name, v.variant_name);
                const payload_len: usize = if (v.payload) |p| p.len else 0;
                // Layout: [tag][field0][field1]... — always heap-allocated for uniform get_tag
                try self.writeFmt("{{ kira_int* _v = (kira_int*)KIRA_ALLOC({d} * sizeof(kira_int)); _v[0] = {d}; ", .{ payload_len + 1, tag });
                if (v.payload) |payload| {
                    for (payload, 0..) |p, i| {
                        try self.writeFmt("_v[{d}] = v{d}; ", .{ i + 1, p });
                    }
                }
                try self.writeFmt("v{d} = (kira_int)(intptr_t)_v; }} /* variant {s} */\n", .{ ref, v.variant_name });
            },
            .tuple_get => |t| try self.writeFmt("v{d} = ((kira_int*)(intptr_t)v{d})[{d}];\n", .{ ref, t.tuple, t.index }),
            .field_get => |f| {
                // Look up field index from type declarations, fallback to linear search
                const field_idx = self.lookupFieldIndex(f.field);
                try self.writeFmt("v{d} = ((kira_int*)(intptr_t)v{d})[{d}]; /* .{s} */\n", .{ ref, f.object, field_idx, f.field });
            },
            .index_get => |idx| {
                // Bounds check (enabled by -DKIRA_BOUNDS_CHECK)
                try self.write("#ifdef KIRA_BOUNDS_CHECK\n");
                try self.writeIndent();
                try self.writeFmt("if (v{d} < 0 || v{d} >= ((kira_int*)(intptr_t)v{d})[0]) {{ fprintf(stderr, \"Kira bounds error: index %lld out of range [0, %lld)\\n\", (long long)v{d}, (long long)((kira_int*)(intptr_t)v{d})[0]); abort(); }}\n", .{ idx.index, idx.index, idx.object, idx.index, idx.object });
                try self.write("#endif\n");
                try self.writeIndent();
                // Elements start at index 1 (index 0 is length)
                try self.writeFmt("v{d} = ((kira_int*)(intptr_t)v{d})[v{d} + 1];\n", .{ ref, idx.object, idx.index });
            },
            .array_len => |v| {
                // Length is stored at index 0
                try self.writeFmt("v{d} = ((kira_int*)(intptr_t)v{d})[0];\n", .{ ref, v });
            },
            .field_set => |f| {
                const field_idx = self.lookupFieldIndex(f.field);
                try self.writeFmt("((kira_int*)(intptr_t)v{d})[{d}] = v{d}; v{d} = 0; /* .{s} = */\n", .{ f.object, field_idx, f.value, ref, f.field });
            },
            .index_set => |idx| {
                // Bounds check (enabled by -DKIRA_BOUNDS_CHECK)
                try self.write("#ifdef KIRA_BOUNDS_CHECK\n");
                try self.writeIndent();
                try self.writeFmt("if (v{d} < 0 || v{d} >= ((kira_int*)(intptr_t)v{d})[0]) {{ fprintf(stderr, \"Kira bounds error: index %lld out of range [0, %lld)\\n\", (long long)v{d}, (long long)((kira_int*)(intptr_t)v{d})[0]); abort(); }}\n", .{ idx.index, idx.index, idx.object, idx.index, idx.object });
                try self.write("#endif\n");
                try self.writeIndent();
                // Elements start at index 1 (index 0 is length)
                try self.writeFmt("((kira_int*)(intptr_t)v{d})[v{d} + 1] = v{d}; v{d} = 0;\n", .{ idx.object, idx.index, idx.value, ref });
            },
            .get_tag => |v| {
                // Tag is at index 0 of the heap-allocated variant struct
                try self.writeFmt("v{d} = ((kira_int*)(intptr_t)v{d})[0]; /* get_tag */\n", .{ ref, v });
            },
            .get_payload => |p| {
                // Payload fields start at index 1
                try self.writeFmt("v{d} = ((kira_int*)(intptr_t)v{d})[{d}]; /* get_payload */\n", .{ ref, p.variant, p.field_index + 1 });
            },
            .wrap_some => |v| try self.writeFmt("v{d} = v{d}; /* wrap_some */\n", .{ ref, v }),
            .wrap_none => try self.writeFmt("v{d} = 0; /* wrap_none */\n", .{ref}),
            .wrap_ok => |v| try self.writeFmt("v{d} = v{d}; /* wrap_ok */\n", .{ ref, v }),
            .wrap_err => |v| try self.writeFmt("v{d} = v{d}; /* wrap_err */\n", .{ ref, v }),
            .unwrap => |v| try self.writeFmt("v{d} = v{d}; /* unwrap */\n", .{ ref, v }),
            .str_concat => |sc| {
                // Concatenate string parts using runtime helper
                if (sc.parts.len == 0) {
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)\"\";\n", .{ref});
                } else if (sc.parts.len == 1) {
                    try self.writeFmt("v{d} = v{d};\n", .{ ref, sc.parts[0] });
                } else {
                    // Compute total length, allocate, then copy parts
                    try self.writeFmt("{{ size_t _len = 0; ", .{});
                    for (sc.parts) |p| {
                        try self.writeFmt("_len += strlen((const char*)(intptr_t)v{d}); ", .{p});
                    }
                    try self.write("char* _s = (char*)KIRA_ALLOC(_len + 1); _s[0] = '\\0'; ");
                    for (sc.parts) |p| {
                        try self.writeFmt("strcat(_s, (const char*)(intptr_t)v{d}); ", .{p});
                    }
                    try self.writeFmt("v{d} = (kira_int)(intptr_t)_s; }}\n", .{ref});
                }
            },
            .param => |idx| {
                // Map parameter index to parameter name
                if (idx < func.params.len) {
                    const p = func.params[idx];
                    if (std.mem.eql(u8, p.type_name, "string")) {
                        // String params are const char* but SSA vars are kira_int
                        try self.writeFmt("v{d} = (kira_int)(intptr_t){s};\n", .{ ref, sanitizeName(p.name) });
                    } else if (std.mem.eql(u8, p.type_name, "f32") or std.mem.eql(u8, p.type_name, "f64")) {
                        // Float params need memcpy into kira_int
                        try self.writeFmt("memcpy(&v{d}, &{s}, sizeof({s}));\n", .{ ref, sanitizeName(p.name), kiraTypeToCType(p.type_name) });
                    } else {
                        try self.writeFmt("v{d} = {s};\n", .{ ref, sanitizeName(p.name) });
                    }
                } else {
                    try self.writeFmt("v{d} = 0; /* param {d} */\n", .{ ref, idx });
                }
            },
            .capture => |idx| {
                // Load captured value from closure environment (passed as hidden first param)
                // Captures start at index 1 (index 0 is function pointer)
                try self.writeFmt("v{d} = ((kira_int*)(intptr_t)_env)[{d}]; /* capture {d} */\n", .{ ref, idx + 1, idx });
            },
            .store_param => |sp| {
                if (sp.param_index < func.params.len) {
                    try self.writeFmt("{s} = v{d}; /* TCO param reassign */\n", .{ func.params[sp.param_index].name, sp.value });
                }
            },
            .method_call => |mc| {
                const fn_name = if (func.name) |n| n else "anonymous";
                try self.writeFmt("v{d} = 0; fprintf(stderr, \"runtime: unresolved method_call .{s} in function '{s}' (v{d})\\n\"); abort();\n", .{ ref, mc.method, fn_name, ref });
            },
            .phi => |p| {
                // Phi nodes are handled by placing values in the predecessor blocks
                // For C, we just declare the variable (already done above)
                try self.writeFmt("/* phi: {d} incoming */\n", .{p.incoming.len});
            },
        }
    }

    // ----------------------------------------------------------------
    // Terminator emission
    // ----------------------------------------------------------------

    fn emitTerminator(self: *CCodeGen, func: *const Function, blk: *const ir.BasicBlock, term: *const Terminator) CodeGenError!void {
        try self.writeIndent();
        switch (term.*) {
            .ret => |v| {
                // Effect tracking: pure functions leave pure context before returning
                if (!func.is_effect) {
                    try self.write("KIRA_LEAVE_PURE(); ");
                }
                const is_void = std.mem.eql(u8, func.return_type_name, "void");
                if (is_void) {
                    try self.write("return;\n");
                } else if (v) |val| {
                    const rtype = kiraTypeToCType(func.return_type_name);
                    // Cast if return type differs from kira_int
                    if (std.mem.eql(u8, rtype, "const char*")) {
                        // String: int → pointer needs intptr_t cast
                        try self.writeFmt("return (const char*)(intptr_t)v{d};\n", .{val});
                    } else if (!std.mem.eql(u8, rtype, "kira_int") and !std.mem.eql(u8, rtype, "int64_t")) {
                        try self.writeFmt("return ({s})v{d};\n", .{ rtype, val });
                    } else {
                        try self.writeFmt("return v{d};\n", .{val});
                    }
                } else {
                    try self.write("return 0;\n");
                }
            },
            .jump => |b| {
                try self.emitPhiAssignmentsForEdge(func, blk.id, b);
                try self.writeIndent();
                try self.writeFmt("goto bb{d};\n", .{b});
            },
            .branch => |br| {
                // Check if either target has phi nodes
                const then_has_phi = self.blockHasPhi(func, br.then_block);
                const else_has_phi = self.blockHasPhi(func, br.else_block);
                if (then_has_phi or else_has_phi) {
                    // Use if/else blocks to emit phi assignments per edge
                    try self.writeFmt("if (v{d}) {{\n", .{br.condition});
                    self.indent_level += 1;
                    try self.emitPhiAssignmentsForEdge(func, blk.id, br.then_block);
                    try self.writeIndent();
                    try self.writeFmt("goto bb{d};\n", .{br.then_block});
                    self.indent_level -= 1;
                    try self.writeIndent();
                    try self.write("} else {\n");
                    self.indent_level += 1;
                    try self.emitPhiAssignmentsForEdge(func, blk.id, br.else_block);
                    try self.writeIndent();
                    try self.writeFmt("goto bb{d};\n", .{br.else_block});
                    self.indent_level -= 1;
                    try self.writeIndent();
                    try self.write("}\n");
                } else {
                    try self.writeFmt("if (v{d}) goto bb{d}; else goto bb{d};\n", .{
                        br.condition,
                        br.then_block,
                        br.else_block,
                    });
                }
            },
            .switch_tag => |sw| {
                try self.writeFmt("switch (v{d}) {{\n", .{sw.value});
                self.indent_level += 1;
                for (sw.cases) |c| {
                    try self.writeIndent();
                    try self.writeFmt("case {d}: goto bb{d};\n", .{ c.tag, c.block });
                }
                try self.writeIndent();
                try self.writeFmt("default: goto bb{d};\n", .{sw.default});
                self.indent_level -= 1;
                try self.writeIndent();
                try self.write("}\n");
            },
            .unreachable_term => {
                try self.write("__builtin_unreachable();\n");
            },
        }
    }

    // ----------------------------------------------------------------
    // Helpers
    // ----------------------------------------------------------------

    /// Look up the tag value for a variant by searching the module's type declarations.
    fn lookupVariantTag(self: *const CCodeGen, type_name: ?[]const u8, variant_name: []const u8) u32 {
        const module = self.current_module orelse return 0;
        for (module.type_decls.items) |td| {
            switch (td.kind) {
                .sum_type => |st| {
                    // If type_name is provided, only search in the matching type
                    if (type_name) |tn| {
                        if (!std.mem.eql(u8, td.name, tn)) continue;
                    }
                    for (st.variants) |v| {
                        if (std.mem.eql(u8, v.name, variant_name)) return v.tag;
                    }
                },
                .product_type => {},
            }
        }
        return 0;
    }

    /// Look up the index of a field by name, searching all product type declarations.
    fn lookupFieldIndex(self: *const CCodeGen, field_name: []const u8) u32 {
        const module = self.current_module orelse return 0;
        for (module.type_decls.items) |td| {
            switch (td.kind) {
                .product_type => |pt| {
                    for (pt.fields) |f| {
                        if (std.mem.eql(u8, f.name, field_name)) return f.index;
                    }
                },
                .sum_type => {},
            }
        }
        return 0;
    }

    // ----------------------------------------------------------------
    // Type mapping
    // ----------------------------------------------------------------

    /// Map a Kira source-level type name to the corresponding C type string.
    /// Sanitize a Kira identifier for use as a C variable name.
    /// Prefixes C keywords with an underscore.
    fn sanitizeName(name: []const u8) []const u8 {
        const c_keywords = [_][]const u8{
            "auto",     "break",    "case",     "char",   "const",    "continue",
            "default",  "do",       "double",   "else",   "enum",     "extern",
            "float",    "for",      "goto",     "if",     "int",      "long",
            "register", "return",   "short",    "signed", "sizeof",   "static",
            "struct",   "switch",   "typedef",  "union",  "unsigned", "void",
            "volatile", "while",
        };
        for (c_keywords) |kw| {
            if (std.mem.eql(u8, name, kw)) return "_kira_reserved";
        }
        return name;
    }

    fn kiraTypeToCType(type_name: []const u8) []const u8 {
        if (std.mem.eql(u8, type_name, "i8")) return "int8_t";
        if (std.mem.eql(u8, type_name, "i16")) return "int16_t";
        if (std.mem.eql(u8, type_name, "i32")) return "int32_t";
        if (std.mem.eql(u8, type_name, "i64")) return "int64_t";
        if (std.mem.eql(u8, type_name, "i128")) return "__int128";
        if (std.mem.eql(u8, type_name, "u8")) return "uint8_t";
        if (std.mem.eql(u8, type_name, "u16")) return "uint16_t";
        if (std.mem.eql(u8, type_name, "u32")) return "uint32_t";
        if (std.mem.eql(u8, type_name, "u64")) return "uint64_t";
        if (std.mem.eql(u8, type_name, "u128")) return "unsigned __int128";
        if (std.mem.eql(u8, type_name, "f32")) return "float";
        if (std.mem.eql(u8, type_name, "f64")) return "double";
        if (std.mem.eql(u8, type_name, "bool")) return "bool";
        if (std.mem.eql(u8, type_name, "string")) return "const char*";
        if (std.mem.eql(u8, type_name, "char")) return "uint32_t";
        if (std.mem.eql(u8, type_name, "void")) return "void";
        return "int64_t"; // fallback for compound/unknown types
    }

    // ----------------------------------------------------------------
    // Output helpers
    // ----------------------------------------------------------------

    fn write(self: *CCodeGen, s: []const u8) CodeGenError!void {
        self.output.appendSlice(self.allocator, s) catch return CodeGenError.OutOfMemory;
    }

    fn writeFmt(self: *CCodeGen, comptime fmt: []const u8, args: anytype) CodeGenError!void {
        std.fmt.format(self.output.writer(self.allocator), fmt, args) catch return CodeGenError.OutOfMemory;
    }

    /// Emit a string with C escape sequences for special characters.
    fn emitEscapedString(self: *CCodeGen, s: []const u8) CodeGenError!void {
        for (s) |ch| {
            switch (ch) {
                0 => try self.write("\\0"),
                '\n' => try self.write("\\n"),
                '\r' => try self.write("\\r"),
                '\t' => try self.write("\\t"),
                '\\' => try self.write("\\\\"),
                '"' => try self.write("\\\""),
                else => {
                    var buf: [1]u8 = .{ch};
                    try self.write(&buf);
                },
            }
        }
    }

    fn writeIndent(self: *CCodeGen) CodeGenError!void {
        for (0..self.indent_level) |_| {
            try self.write("    ");
        }
    }
};

